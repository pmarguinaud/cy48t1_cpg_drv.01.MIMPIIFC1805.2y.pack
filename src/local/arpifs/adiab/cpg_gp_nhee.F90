  CALL GPRT(LSPRT,NPROMA,KST,KEND,NFLEVG,RD,RV,&
   & YDCPG_DYN0%RCP%R,ZT0T,ZT0TL,ZT0TM,&
   & ZPQL,ZPQM,&
   & ZRT0,PRT0L,PRT0M)
