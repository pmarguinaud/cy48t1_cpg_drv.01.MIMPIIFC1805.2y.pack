  ZDVER0(KST:KEND,1:NFLEVG)=ZT0SVD(KST:KEND,1:NFLEVG)
  ZDVER0L(KST:KEND,1:NFLEVG)=ZT0SVDL(KST:KEND,1:NFLEVG)
  ZDVER0M(KST:KEND,1:NFLEVG)=ZT0SVDM(KST:KEND,1:NFLEVG)
ELSEIF (NVDVAR == 4) THEN
  ZDVER0(KST:KEND,1:NFLEVG)=ZT0SVD(KST:KEND,1:NFLEVG)-PNHXT0(KST:KEND,1:NFLEVG)
  ZDVER0L(KST:KEND,1:NFLEVG)=ZT0SVDL(KST:KEND,1:NFLEVG)-ZT0NHXL(KST:KEND,1:NFLEVG)
  ZDVER0M(KST:KEND,1:NFLEVG)=ZT0SVDM(KST:KEND,1:NFLEVG)-ZT0NHXM(KST:KEND,1:NFLEVG)
ELSEIF (NVDVAR == 5) THEN
  ! d13 in ZDVERW0
  ZDVERW0(KST:KEND,1:NFLEVG)=ZT0SVD(KST:KEND,1:NFLEVG)-PNHXT0(KST:KEND,1:NFLEVG)
  ! dver in ZDVER0: dver = d13 - (d13 - dver)
  CALL GNHEE_SVDINCR13(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,POROGL,POROGM,&
   & PUVH0(1,0,YYTHW0%M_UH),PUVH0(1,0,YYTHW0%M_VH),&
   & PLNPR=YDCPG_DYN0%XYB%LNPR,PRT=ZRDT0,PNHPPI=ZNHPPI0,&
   & PSVDINCR13=ZSVDINCR13)
  ZDVER0(KST:KEND,1:NFLEVG)=ZDVERW0(KST:KEND,1:NFLEVG)-ZSVDINCR13(KST:KEND,1:NFLEVG)
  ! grad(dver) is not used; ZDVER0[L,M] are used to store grad(d13)
  ZDVER0L(KST:KEND,1:NFLEVG)=ZT0SVDL(KST:KEND,1:NFLEVG)-ZT0NHXL(KST:KEND,1:NFLEVG)
  ZDVER0M(KST:KEND,1:NFLEVG)=ZT0SVDM(KST:KEND,1:NFLEVG)-ZT0NHXM(KST:KEND,1:NFLEVG)
ENDIF

!*     3.1.16 COMPUTES term [gw] or [gW].

! Caution: must use consistent definitions of RT in ZRDT0 and ZDVER0.
IF (NVDVAR==3 .OR. NVDVAR==4) THEN
  LLGWF0=.TRUE.
  LLGDWI=.FALSE.
  IF (LGWADV.AND.LVFE_GW) THEN
    ! PGDW is required (at least for SL2TL)
    CALL GPGW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,LLGWF0,LLGDWI,&
     & POROGL,POROGM,YDCPG_DYN0%XYB%LNPR,YDCPG_DYN0%XYB%ALPH,ZUS0,ZVS0,&
     & ZRDT0,ZDVER0,PGWHT0,PGWFT0,PRNHPPI=ZRNHPPI0,PGDW=PGDW0)

    PGWT0(KST:KEND,1:NFLEVG)=PGWFT0(KST:KEND,1:NFLEVG)
  ELSE
    CALL GPGW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,LLGWF0,LLGDWI,&
     & POROGL,POROGM,YDCPG_DYN0%XYB%LNPR,YDCPG_DYN0%XYB%ALPH,ZUS0,ZVS0,&
     & ZRDT0,ZDVER0,PGWHT0,PGWFT0,PRNHPPI=ZRNHPPI0)

    IF (LGWADV) PGWT0(KST:KEND,1:NFLEVG)=PGWHT0(KST:KEND,0:NFLEVG-1)
  ENDIF

  IF (LTWOTL.AND.LRDBBC) THEN
    ! PGWS contains (gw)_surf
    PGWS(KST:KEND)=PGWHT0(KST:KEND,NFLEVG)
  ENDIF
ELSEIF (NVDVAR==5) THEN
  ! Case LVFE_GW is ignored and not coded.
  ! -- Modified W from d13; W_surf=0, this is why ZZERO enters GPGW instead of
  ! (POROGL,POROGM,ZUS0,ZVS0)
  LLGWF0=.TRUE.
  LLGDWI=.FALSE.
  CALL GPGW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,LLGWF0,LLGDWI,&
   & ZZERO,ZZERO,YDCPG_DYN0%XYB%LNPR,YDCPG_DYN0%XYB%ALPH,ZZERO,ZZERO,&
   & ZRDT0,ZDVERW0,PGWHT0,PGWFT0,PRNHPPI=ZRNHPPI0)
  IF (LGWADV) THEN
    PGWT0(KST:KEND,1:NFLEVG)=PGWHT0(KST:KEND,0:NFLEVG-1)
  ENDIF

  IF (LTWOTL.AND.LRDBBC) THEN
    ! PGWS contains 0
    PGWS(KST:KEND)=0.0_JPRB
  ENDIF
ENDIF

!*     3.1.17 COMPUTES grad(gw) or  grad(gW).

! Caution: must use consistent definitions of RT in ZRDT0,ZRDT0L,ZRDT0M, and
! ZDVER0,ZDVER0L,ZDVER0M.
LLDER=.TRUE.
IF (NVDVAR==3 .OR. NVDVAR==4) THEN
  CALL GNHGRGW(YDGEOMETRY,NPROMA,NFLEVG,KST,KEND,LLDER,&
   & ZRDT0,ZRDT0L,ZRDT0M,&
   & ZDVER0,ZDVER0L,ZDVER0M,&
   & YDCPG_DYN0%XYB%LNPR,YDCPG_DYN0%XYB%ALPH,ZXYBDER0,&
   & ZUS0,ZVS0,ZUS0_L,ZVS0_L,ZUS0_M,ZVS0_M,&
   & POROGL,POROGM,POROGLM,POROGLL,POROGMM,&
   & PGWFL,PGWFM,ZGWHL,ZGWHM,&
   & LDNHEE=.TRUE.,PRNHPPI=ZRNHPPI0,PQCHAL=PQCHA0L,PQCHAM=PQCHA0M)
ELSEIF (NVDVAR==5) THEN
  ! -- grad(gW) from d13 and grad(d13); grad(W)_surf=0, this is why ZZERO enters
  ! GNHGRGW instead of (POROG.. and Z[U,V]S0.. arrays)
  CALL GNHGRGW(YDGEOMETRY,NPROMA,NFLEVG,KST,KEND,LLDER,&
   & ZRDT0,ZRDT0L,ZRDT0M,&
   & ZDVERW0,ZDVER0L,ZDVER0M,&
   & YDCPG_DYN0%XYB%LNPR,YDCPG_DYN0%XYB%ALPH,ZXYBDER0,&
   & ZZERO,ZZERO,ZZERO,ZZERO,ZZERO,ZZERO,&
   & ZZERO,ZZERO,ZZERO,ZZERO,ZZERO,&
   & PGWFL,PGWFM,ZGWHL,ZGWHM,&
   & LDNHEE=.TRUE.,PRNHPPI=ZRNHPPI0,PQCHAL=PQCHA0L,PQCHAM=PQCHA0M)
ENDIF

!*     3.1.18 COMPUTES D3.

CALL GNHD3(NFLEVG,NPROMA,KST,KEND,&
 & ZT0DIV,ZNHXS,ZDVER0,YDCPG_DYN0%RCP%R,RMAX_D3,LBOUND_D3,Z3DIVG)

!*     3.1.19 COMPUTES pre/(Rd T prehyd [Delta log(prehyd)]) at t.

IF (.NOT.LGWADV) THEN
  IF (L_RDRY_VD) THEN
    ! ky: caution, PRDPHI is currently computed with R instead of Rd (by error),
    !  but Rd would be the right value.
    !  The right code should be:
    !  PRDPHI(.,.)=1.0_JPRB/(PXYB0(.,.,YYTXYB0%M_LNPR)*RD*PGMV(.,.,YT0%MT)*ZRNHPPI0(.,.))
    DO JLEV=1,NFLEVG
      DO JROF=KST,KEND
        PRDPHI(JROF,JLEV)=1.0_JPRB/(YDCPG_DYN0%XYB%LNPR(JROF,JLEV)*ZRTR0(JROF,JLEV))
      ENDDO
    ENDDO
  ELSE
    ! ky: PRDPHI uses the right (moist) version of "R" in this case.
    DO JLEV=1,NFLEVG
      DO JROF=KST,KEND
        PRDPHI(JROF,JLEV)=1.0_JPRB/(YDCPG_DYN0%XYB%LNPR(JROF,JLEV)*ZRTR0(JROF,JLEV))
      ENDDO
    ENDDO
  ENDIF
ENDIF

!*     3.1.20a COMPUTES [D V/Dt].

! Pressure gradient term + explicit Coriolis + Rayleigh friction
CALL GP_TNDLAGADIAB_UV(YDGEOMETRY,YDGMV,YDMODEL%YRML_PHY_EC%YREPHY,YDMODEL%YRML_DYN%YRDYN,&
 & KST,KEND,YDGSGEOM%RCORI,&
 & YDGSGEOM%GNORDL,YDGSGEOM%GNORDM,&
 & ZPSGRTL,ZPSGRTM,&
 & PGMV,PATND(1,1,YYTTND%M_TNDU),PATND(1,1,YYTTND%M_TNDV),&
 & PATND(1,1,YYTTND%M_TNDU_NOC),PATND(1,1,YYTTND%M_TNDV_NOC))

!*     3.1.20b COMPUTES [D V_surf/Dt].

IF (.NOT.LGWADV.OR.LNHEE_SVDLAPL_FIRST.OR.LNHEE_REFINE_PREH_BBC) THEN
  CALL GNH_TNDLAGADIAB_UVS(NPROMA,KST,KEND,LRPLANE,&
   & YDGSGEOM%RCORI,YDCSGEOM%RATATX,YDCSGEOM%RATATH,&
   & ZUS0,ZVS0,ZSGRTSL,ZSGRTSM,&
   & ZTNDUS,ZTNDVS)
ENDIF

!*     3.1.20c REFINE half-level pre, pres. grad. term, [D V/Dt] at bottom

IF (LNHEE_REFINE_PREH_BBC) THEN

  ! remark (K.Y.) for option LGRADSP: filter is applied to main part of the
  ! pressure gradient term computed in the predictor step,                
  ! but no filter is applied to the correction done in GNHEE_REFINE_PREH.
  ! But I do not guarantee that LGRADSP=T and LNHEE_REFINE_PREH_BBC=T are
  ! consistent.

  CALL GNHEE_REFINE_PREH(YDGEOMETRY,KST,KEND,&
    & POROGL,POROGM,POROGLM,POROGLL,POROGMM,&
    & ZUS0,ZVS0,PRE0F,PRE0,ZPDEP0,ZWH2F,&
    & ZPSGRTL,ZPSGRTM,ZSGRTSL,ZSGRTSM,&
    & PATND(1,1,YYTTND%M_TNDU),PATND(1,1,YYTTND%M_TNDV),&
    & PATND(1,1,YYTTND%M_TNDU_NOC),PATND(1,1,YYTTND%M_TNDV_NOC),&
    & ZTNDUS,ZTNDVS,PNHPRE0H)

ENDIF

!*     3.1.21 COMPUTES [D(gw)/Dt].

IF (LNHEE_SVDLAPL_FIRST) THEN
  ! * Laplacian term computed first, then rhs of (gw) eqn computed.
  !   GNHEE_TNDLAGADIAB_GW is called for LGWADV=T only.
  IF (LGWADV) THEN
    ! warning: formerly LVFE_LAPL.OR.LVFE_GW
    IF (LVFE_LAPL_BC.OR.LVFE_GW) CALL ABOR1(' CPG_GP_NHEE 3.1.21: option not yet coded!')

    CALL GNHEE_TNDLAGADIAB_GW(YDGEOMETRY,KST,KEND,&
     & POROGL,POROGM,POROGLM,POROGLL,POROGMM,&
     & ZUS0,ZVS0,ZTNDUS,ZTNDVS,&
     & YDCPG_DYN0%XYB%LNPR,YDCPG_DYN0%XYB%ALPH,PRE0F,PRE0,ZPDEP0,&
     & PATND(1,1,YYTTND%M_TNDGW))
  ENDIF
ELSE
  ! * rhs of (gw) eqn computed first, then Laplacian term in "dver" eqn computed.
  !   GNH_TNDLAGADIAB_GW may be called for both LGWADV=T and LGWADV=F.
  !   PDBBC is required only if LGWADV=F.
  !   Dataflow may be tricky, and not always convenient to test future variable changes.

  IF (LGWADV.AND.LVFE_GW) THEN
    CALL GNH_TNDLAGADIAB_GW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,&
     & PDELP=YDCPG_DYN0%XYB%DELP,PDELNHPRE=ZDELNHPRE0,&
     & PTNDGWF_LAP=ZTNDGWF_LAP,PTNDGWF_OTH=ZTNDGWF_OTH)
    DO JLEV=1,NFLEVG
      DO JROF=KST,KEND
        PATND(JROF,JLEV,YYTTND%M_TNDGW)=ZTNDGWF_LAP(JROF,JLEV)+ZTNDGWF_OTH(JROF,JLEV)
      ENDDO
    ENDDO
  ELSEIF (LGWADV) THEN
    CALL GNH_TNDLAGADIAB_GW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,&
     & PDEP=ZPDEP0,PREF=PRE0F,PREH=PRE0,&
     & PTNDGWH_LAP=ZTNDGWH_LAP,PTNDGWH_OTH=ZTNDGWH_OTH)
    DO JLEV=1,NFLEVG
      DO JROF=KST,KEND
        PATND(JROF,JLEV,YYTTND%M_TNDGW)=ZTNDGWH_LAP(JROF,JLEV-1)+ZTNDGWH_OTH(JROF,JLEV-1)
      ENDDO
    ENDDO
  ELSE IF (LVFE_GW) THEN
    CALL GNH_TNDLAGADIAB_GW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,&
     & POROGL=POROGL,POROGM=POROGM,&
     & POROGLM=POROGLM,POROGLL=POROGLL,POROGMM=POROGMM,&
     & PUS=ZUS0,PVS=ZVS0,PTNDUS=ZTNDUS,PTNDVS=ZTNDVS,&
     & PDEP=ZPDEP0,PREF=PRE0F,PREH=PRE0,&
     & PDELP=YDCPG_DYN0%XYB%DELP,PDELNHPRE=ZDELNHPRE0,&
     & PTNDGWH_LAP=ZTNDGWH_LAP,PTNDGWF_OTH=ZTNDGWF_OTH,PDBBC=PDBBC)
  ELSE
    DO JROF=KST,KEND
      ZPDEPS0(JROF)=PNHPRE0H(JROF,NFLEVG)-PRE0(JROF,NFLEVG)
    ENDDO

    CALL GNH_TNDLAGADIAB_GW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,&
     & POROGL=POROGL,POROGM=POROGM,&
     & POROGLM=POROGLM,POROGLL=POROGLL,POROGMM=POROGMM,&
     & PUS=ZUS0,PVS=ZVS0,PTNDUS=ZTNDUS,PTNDVS=ZTNDVS,&
     & PDEP=ZPDEP0,PDEPS=ZPDEPS0,PREF=PRE0F,PREH=PRE0,&
     & PTNDGWH_LAP=ZTNDGWH_LAP,PTNDGWH_OTH=ZTNDGWH_OTH,PDBBC=PDBBC)
  ENDIF
ENDIF

!*     3.1.22 COMPUTES [D(pressure departure variable)/Dt].

CALL GNH_TNDLAGADIAB_SPD(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,&
 & YDCPG_DYN0%RCP%KAP,Z3DIVG,PCTY0(1,1,YYTCTY0%M_VVEL),&
 & PATND(1,1,YYTTND%M_TNDPD) )

!*     3.1.23 COMPUTES [D(vertical divergence variable)/Dt].

IF (.NOT.LGWADV) THEN
  ! Remark: PATND(.,.,YYTTND%M_TNDVD) is useless (and not computed) if LGWADV.
  IF (LNHEE_SVDLAPL_FIRST) THEN
    ! * Laplacian term directly computed, does not require the rhs of (gw) eqn.
    !   GNHEE_TNDLAGADIAB_SVD is called for LGWADV=F only.
    IF (LVERTFE) CALL ABOR1(' CPG_GP_NHEE 3.1.23: not yet coded case')

    CALL GNHEE_TNDLAGADIAB_SVD(YDGEOMETRY,KST,KEND,&
     & POROGL,POROGM,POROGLM,POROGLL,POROGMM,&
     & PCTY0(1,0,YYTCTY0%M_EVEL),YDCPG_DYN0%XYB%RDELP,&
     & YDCPG_DYN0%XYB%LNPR,YDCPG_DYN0%XYB%ALPH,PRE0F,PRE0,ZPDEP0,&
     & ZT0U,ZT0V,PUVH0(1,0,YYTHW0%M_UH),PUVH0(1,0,YYTHW0%M_VH),&
     & PATND(1,1,YYTTND%M_TNDU),PATND(1,1,YYTTND%M_TNDV),ZTNDUS,ZTNDVS,&
     & PRDPHI,ZGWHL,ZGWHM,PGWFL,PGWFM,ZDVER0,ZDVERW0,ZT0DIV,Z3DIVG,&
     & PATND(1,1,YYTTND%M_TNDVD),PDBBC)
  ELSE
    ! * rhs of (gw) eqn computed first, then Laplacian term in "dver" eqn computed.
    !   GNH_TNDLAGADIAB_GW must have been called above to provide ZTNDGW.. arrays.
    !   Dataflow may be tricky, and not always convenient to test future variable changes.
    CALL GNH_TNDLAGADIAB_SVD(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,&
     & ZT0U,ZT0V,PUVH0(1,0,YYTHW0%M_UH),PUVH0(1,0,YYTHW0%M_VH),&
     & PRDPHI,PGWHT0,PGWFT0,ZGWHL,ZGWHM,PGWFL,PGWFM,ZDVER0,ZT0DIV,Z3DIVG,&
     & ZTNDGWH_LAP,ZTNDGWH_OTH,ZTNDGWF_OTH,&
     & ZPDEP0,PRE0,PRE0F,YDCPG_DYN0%XYB%DELP,YDCPG_DYN0%XYB%LNPR,&
     & PATND(1,1,YYTTND%M_TNDVD) )
  ENDIF
ENDIF

!*     3.1.24 COMPUTES [DT/Dt].

DO JLEV=1,NFLEVG
  DO JROF=KST,KEND
    ZRCV=1.0_JPRB/(1.0_JPRB-YDCPG_DYN0%RCP%KAP(JROF,JLEV))-1.0_JPRB
    PATND(JROF,JLEV,YYTTND%M_TNDT)=-ZRCV*ZT0T(JROF,JLEV)*Z3DIVG(JROF,JLEV)
  ENDDO
ENDDO

!---------------------------------------------------
!      3.2         TIME t9 CALCULATIONS
!---------------------------------------------------

IF (.NOT.LTWOTL .AND. NCURRENT_ITER == 0) THEN

!*     3.2.1 COMPUTE PRE9, PXYB9, PRE9F.

  CALL GPHPRE(NPROMA,NFLEVG,KST,KEND,YDVAB,PRE9,PXYB=PXYB9,PRESF=PRE9F)

!*     3.2.3 COMPUTE "R", "Cp" AND "Kap=R/Cp".

  CALL GPRCP(NPROMA,KST,KEND,NFLEVG,PGFL=PGFL,KGFLTYP=9,PCP=YDCPG_DYN9%RCP%CP,&
   & PR=YDCPG_DYN9%RCP%R)

!*     3.2.4 COMPUTE "RT".

  ! ZR0T9 is used in calculation of NHX(t-dt).
  ZR0T9(KST:KEND,1:NFLEVG)=ZT9T(KST:KEND,1:NFLEVG)*YDCPG_DYN0%RCP%R(KST:KEND,1:NFLEVG)

  ! ZRDT9 is the (RT) at "t-dt" used in the definition of "dver":
  IF (L_RDRY_VD) THEN
    ZRDT9(KST:KEND,1:NFLEVG)=RD*ZT9T(KST:KEND,1:NFLEVG)
  ELSE
    ! use of R_moist at time t to be consistent with ZR0T9 and ZDVER9.
    DO JLEV=1,NFLEVG
      DO JROF=KST,KEND
        ZRDT9(JROF,JLEV)=YDCPG_DYN0%RCP%R(JROF,JLEV)*ZT9T(JROF,JLEV)
      ENDDO
    ENDDO
  ENDIF

!*     3.2.6 COMPUTE QUANTITIES FOR NHEE MODEL.

  CALL GNHPRE(YDGEOMETRY,NPROMA,NFLEVG,KST,KEND,ZT9SPD,PRE9F,&
   & PNHPREF=PNHPRE9F,PNHPPI=ZNHPPI9,PRNHPPI=ZRNHPPI9,PDEP=ZPDEP9)
  DO JLEV=1,NFLEVG
    ZRRED9(KST:KEND,JLEV)=YDCPG_DYN9%RCP%R(KST:KEND,JLEV)/ZNHPPI9(KST:KEND,JLEV)
  ENDDO
  CALL GNHPREH(YDGEOMETRY,NPROMA,NFLEVG,KST,KEND,ZT9SPD,PRE9,&
   & YDCPG_DYN9%XYB%DELP,YDCPG_DYN9%XYB%LNPR,ZNHPPI9,PNHPRE9F,PNHPRE9H,ZDELNHPRE9)

!*     3.2.8 COMPUTES THE GEOPOTENTIAL HEIGHT "gz" AND ITS GRADIENT.

  ! * "gz" at full levels and half levels.
  IF (LDLDIAB.OR.LSIMPH) THEN
    PHI9(KST:KEND,NFLEVG)=POROG(KST:KEND)
    CALL GPGEO(NPROMA,KST,KEND,NFLEVG,PHI9,PHIF9,ZT9T,&
     & ZRRED9,YDCPG_DYN9%XYB%LNPR,YDCPG_DYN9%XYB%ALPH,&
     & YDGEOMETRY%YRVERT_GEOM)
  ENDIF

!*     3.2.10 COMPUTES HALF-LEVEL WINDS.

  ! Not needed if Z-term and X-term are calculated FE
  IF (LDGPXX .AND. .NOT.LVERTFE) THEN
    ! * same remark as for time t half-level winds.
    ZUVH9(KST:KEND,1:NFLEVG,YYTHW9%M_WWI)=PUVH0(KST:KEND,1:NFLEVG,YYTHW0%M_WWI)
    CALL GPHLUV(YDGEOMETRY%YRDIMV,NPROMA,KST,KEND,ZT9U,ZT9V,ZUVH9)
  ENDIF

  LLDER=.FALSE.
  CALL GPUVS(NFLEVG,NPROMA,KST,KEND,LLDER,ZT9U,ZT9V,ZUS9,ZVS9)

!*     3.2.13a COMPUTES TERM "NHX".

  ! caution: must use "R" and "pre/prehyd" at time t.
  IF (LDGPXX) THEN
    CALL GPXX(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,ZGPHL,ZGPHM,PHI0FL,PHI0FM,&
     & YDCPG_DYN9%XYB%LNPR,ZR0T9,&
     & ZT9U,ZT9V,ZUVH9(1,0,YYTHW9%M_UH),ZUVH9(1,0,YYTHW9%M_VH),&
     & ZNHXS,PNHPPI=ZNHPPI0)
    IF (NVDVAR == 3 .OR. NVDVAR == 4) THEN
      ! NHX = NHX_S
      PNHXT9(KST:KEND,1:NFLEVG)=ZNHXS(KST:KEND,1:NFLEVG)
    ELSEIF (NVDVAR == 5) THEN
      ! NHX = NHX_S - (d13 - dver)
      CALL GNHEE_SVDINCR13(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,POROGL,POROGM,&
       & ZUVH9(1,0,YYTHW9%M_UH),ZUVH9(1,0,YYTHW9%M_VH),&
       & PLNPR=YDCPG_DYN9%XYB%LNPR,PRT=ZRDT9,PNHPPI=ZNHPPI9,&
       & PSVDINCR13=ZSVDINCR13)
      PNHXT9(KST:KEND,1:NFLEVG)=ZNHXS(KST:KEND,1:NFLEVG)-ZSVDINCR13(KST:KEND,1:NFLEVG)
    ENDIF
  ELSE
    ! This case only occurs for a subset of NVDVAR= 4 or 5 cases
    ! PNHXT9 and PGMV.,.,YT9%MNHX) always stores NHX; calculation of NHX_S is
    ! useless.
    PNHXT9(KST:KEND,1:NFLEVG)=ZT9NHX(KST:KEND,1:NFLEVG)
  ENDIF

  !*     3.1.13b COMPUTES TERM "NHY"

  IF (NVDVAR == 5 .AND. LGWADV) THEN
    CALL GPYY(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,POROGL,POROGM,&
     & ZUVH9(1,0,YYTHW9%M_UH),ZUVH9(1,0,YYTHW9%M_VH),PNHYT9)
  ELSE
    PNHYT9(KST:KEND,0:NFLEVG) = 0.0_JPRB
  ENDIF


!*     3.2.14 COMPUTES dver.

  IF (NVDVAR == 3) THEN
    ZDVER9(KST:KEND,1:NFLEVG)=ZT9SVD(KST:KEND,1:NFLEVG)
  ELSEIF (NVDVAR == 4) THEN
    ZDVER9(KST:KEND,1:NFLEVG)=ZT9SVD(KST:KEND,1:NFLEVG)-PNHXT9(KST:KEND,1:NFLEVG)
  ELSEIF (NVDVAR == 5) THEN
    ! d13 in ZDVERW9
    ZDVERW9(KST:KEND,1:NFLEVG)=ZT9SVD(KST:KEND,1:NFLEVG)-PNHXT9(KST:KEND,1:NFLEVG)
    ! dver in ZDVER9
    CALL GNHEE_SVDINCR13(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,POROGL,POROGM,&
     & ZUVH9(1,0,YYTHW9%M_UH),ZUVH9(1,0,YYTHW9%M_VH),&
     & PLNPR=YDCPG_DYN9%XYB%LNPR,PRT=ZRDT9,PNHPPI=ZNHPPI9,&
     & PSVDINCR13=ZSVDINCR13)
    ZDVER9(KST:KEND,1:NFLEVG)=ZDVERW9(KST:KEND,1:NFLEVG)-ZSVDINCR13(KST:KEND,1:NFLEVG)
  ENDIF

!*     3.2.16 COMPUTES term [gw] or [gW].

  ! Caution: must use consistent definitions of RT in ZRDT9 and ZDVER9.
  IF (NVDVAR==3 .OR. NVDVAR==4) THEN
    LLGDWI=.FALSE.
    IF (LGWADV.AND.LVFE_GW) THEN
      ! PGDW is required (at least for SL3TL)
      CALL GPGW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,LDMPA,LLGDWI,&
       & POROGL,POROGM,YDCPG_DYN9%XYB%LNPR,YDCPG_DYN9%XYB%ALPH,ZUS9,ZVS9,&
       & ZRDT9,ZDVER9,PGWHT9,PGWFT9,PRNHPPI=ZRNHPPI9,PGDW=PGDW9)

      PGWT9(KST:KEND,1:NFLEVG)=PGWFT9(KST:KEND,1:NFLEVG)
    ELSE
      CALL GPGW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,LDMPA,LLGDWI,&
       & POROGL,POROGM,YDCPG_DYN9%XYB%LNPR,YDCPG_DYN9%XYB%ALPH,ZUS9,ZVS9,&
       & ZRDT9,ZDVER9,PGWHT9,PGWFT9,PRNHPPI=ZRNHPPI9)

      IF (LGWADV) PGWT9(KST:KEND,1:NFLEVG)=PGWHT9(KST:KEND,0:NFLEVG-1)
    ENDIF

    IF (LRDBBC) THEN
      ! PGWS contains (gw)_surf
      PGWS(KST:KEND)=PGWHT9(KST:KEND,NFLEVG)
    ENDIF
  ELSEIF (NVDVAR==5) THEN
    ! Case LVFE_GW is ignored and not coded.
    ! -- Modified W
    LLGDWI=.FALSE.
    CALL GPGW(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,LDMPA,LLGDWI,&
     & ZZERO,ZZERO,YDCPG_DYN9%XYB%LNPR,YDCPG_DYN9%XYB%ALPH,ZZERO,ZZERO,&
     & ZRDT9,ZDVERW9,PGWHT9,PGWFT9,PRNHPPI=ZRNHPPI9)

    IF (LGWADV) PGWT9(KST:KEND,1:NFLEVG)=PGWHT9(KST:KEND,0:NFLEVG-1)

    IF (LRDBBC) THEN
      ! PGWS contains 0
      PGWS(KST:KEND)=0.0_JPRB
    ENDIF
  ENDIF

