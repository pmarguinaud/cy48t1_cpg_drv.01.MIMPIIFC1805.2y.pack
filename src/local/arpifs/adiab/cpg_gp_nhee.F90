     & PLNPR=YDCPG_DYN0%XYB%LNPR,PRT=ZRDT0,PNHPPI=ZNHPPI0,&
     & PSVDINCR13=ZSVDINCR13)
    PNHXT0(KST:KEND,1:NFLEVG)=ZNHXS(KST:KEND,1:NFLEVG)-ZSVDINCR13(KST:KEND,1:NFLEVG)
  ENDIF
ELSE
  ! This case only occurs for a subset of NVDVAR= 4 or 5 cases
  IF (NVDVAR == 4) THEN
    PNHXT0(KST:KEND,1:NFLEVG)=ZT0NHX(KST:KEND,1:NFLEVG)
    ! NHX_S
    ZNHXS(KST:KEND,1:NFLEVG) =ZT0NHX(KST:KEND,1:NFLEVG)
  ELSEIF (NVDVAR == 5) THEN
    PNHXT0(KST:KEND,1:NFLEVG)=ZT0NHX(KST:KEND,1:NFLEVG)
    ! NHX_S = NHX + (d13 - dver)
    CALL GNHEE_SVDINCR13(YDGEOMETRY,NFLEVG,NPROMA,KST,KEND,POROGL,POROGM,&
     & PUVH0(1,0,YYTHW0%M_UH),PUVH0(1,0,YYTHW0%M_VH),&
     & PLNPR=YDCPG_DYN0%XYB%LNPR,PRT=ZRDT0,PNHPPI=ZNHPPI0,&
     & PSVDINCR13=ZSVDINCR13)
    ZNHXS(KST:KEND,1:NFLEVG)=ZT0NHX(KST:KEND,1:NFLEVG)+ZSVDINCR13(KST:KEND,1:NFLEVG)
  ENDIF
