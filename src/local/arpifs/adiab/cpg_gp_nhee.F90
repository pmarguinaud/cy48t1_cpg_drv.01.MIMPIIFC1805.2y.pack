! ZRDT0 contains (RT) at "t" used in the definition of "dver":
IF (L_RDRY_VD) THEN
  ZRDT0(KST:KEND,1:NFLEVG)=RD*ZT0T(KST:KEND,1:NFLEVG)
  ! ky: remark for LSPRT=T:
