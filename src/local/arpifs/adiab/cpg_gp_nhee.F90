CALL GNHPREH(YDGEOMETRY,NPROMA,NFLEVG,KST,KEND,ZT0SPD,PRE0,&
 & YDCPG_DYN0%XYB%DELP,YDCPG_DYN0%XYB%LNPR,ZNHPPI0,PNHPRE0F,PNHPRE0H,ZDELNHPRE0)
CALL GNHGRPRE(YDGEOMETRY,NPROMA,NFLEVG,KST,KEND,YDCPG_DYN0%XYB%RTGR,PRE0L,PRE0M,&
