IF (.NOT.LGWADV) THEN
  IF (LVERTFE) CALL ABOR1(' CPG_GP_NHQE 3.1.23: not yet coded case')

  CALL GNHQE_TNDLAGADIAB_SVD(YDGEOMETRY, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_DYN0%OROGL, YDCPG_DYN0%OROGM, &
