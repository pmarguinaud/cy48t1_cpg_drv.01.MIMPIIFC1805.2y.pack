  CALL GPRCP_YDVARS (NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, NFLEVG, YDVARS=YDVARS, KGFLTYP=9, &
                   & PCP=YDCPG_DYN9%RCP%CP, PR=YDCPG_DYN9%RCP%R, PKAP=YDCPG_DYN9%RCP%KAP)

