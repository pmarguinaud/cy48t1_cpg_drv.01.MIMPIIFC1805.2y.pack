Z_SGRTSM_T0(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=Z_PSGRTM_T0(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,NFLEVG)

