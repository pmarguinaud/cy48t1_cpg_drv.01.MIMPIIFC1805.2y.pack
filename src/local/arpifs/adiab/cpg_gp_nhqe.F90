  DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
    Z_DVER_T0(JROF,JLEV)=YDVARS%SVD%T0(JROF,JLEV)-YDCPG_DYN0%NHX(JROF,JLEV)
    Z_DVERL_T0(JROF,JLEV)=YDVARS%SVD%DL(JROF,JLEV)-YDVARS%NHX%DL(JROF,JLEV)
