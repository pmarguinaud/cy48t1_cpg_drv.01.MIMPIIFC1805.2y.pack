  CALL GPHPRE(NPROMA, NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVAB, YDCPG_DYN9%PRE, PRESF=YDCPG_DYN9%PREF,   &
