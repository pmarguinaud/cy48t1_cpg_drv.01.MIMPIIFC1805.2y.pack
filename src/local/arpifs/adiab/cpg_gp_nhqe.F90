  ! * compute NHX_s = "wind shear" part of NHX
  CALL GPXX(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, Z_GPHL_T0, Z_GPHM_T0, YDCPG_DYN0%PHIFL, &
  & YDCPG_DYN0%PHIFM, YDCPG_DYN0%XYB%LNPR, Z_RT_T0, YDVARS%U%T0, YDVARS%V%T0, YDCPG_DYN0%UVH%UH, YDCPG_DYN0%UVH%VH, &
  & Z_NHXS_T_T0)
  ! * compute NHX_d = "divergence" part of NHX
  CALL GNHQE_XXD(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVARS%U%T0, YDVARS%V%T0, &
  & YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB%RDELP, YDCPG_DYN0%XYB%ALPH, YDCPG_DYN0%XYB%RTGR, YDCPG_DYN0%PREL,  &
