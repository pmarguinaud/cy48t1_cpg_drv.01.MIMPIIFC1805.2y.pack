  & P_GWHT_T0, YDCPG_DYN0%GWFT, PGDW=P_GDW_T0)

  YDCPG_DYN0%GWT(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=YDCPG_DYN0%GWFT(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)
ELSE
  CALL GPGW(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, LLGWF0, LLGDWI, YDCPG_DYN0%OROGL, &
  & YDCPG_DYN0%OROGM, YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB%ALPH, Z_US_T0, Z_VS_T0, Z_RT_T0, Z_DVER_T0,         &
  & P_GWHT_T0, YDCPG_DYN0%GWFT)

  IF (LGWADV) YDCPG_DYN0%GWT(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=P_GWHT_T0(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,0:NFLEVG-1)
