IF (LTWOTL.AND.LRDBBC) THEN
  ! PGWS contains (gw)_surf
  YDCPG_DYN0%GWS(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=P_GWHT_T0(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,NFLEVG)
