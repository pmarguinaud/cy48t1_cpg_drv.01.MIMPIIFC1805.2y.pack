  ! * compute NHX
  DO JLEV=1,NFLEVG
    DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
      YDCPG_DYN0%NHX(JROF,JLEV)=Z_NHXS_T_T0(JROF,JLEV)+Z_NHXD_T_T0(JROF,JLEV)
