ELSE
  YDCPG_DYN0%NHX(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=YDVARS%NHX%T0(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)
  ! ??? But we also need ZNHXS_T0 for RHS of dver equation, and there are two possibilities
  !     which a priori do not give the same results:
  ! ??? first method: compute ZNHXD_T0 via GNHQE_XXD, , then ZNHXS_T0=PNHXT0-ZNHXD_T0
  !     this is the most consistent with what is done in NHEE model
  ! * compute NHX_d
  CALL GNHQE_XXD(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVARS%U%T0, YDVARS%V%T0, &
  & YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB%RDELP, YDCPG_DYN0%XYB%ALPH, YDCPG_DYN0%XYB%RTGR, YDCPG_DYN0%PREL,  &
  & YDCPG_DYN0%PREM, YDCPG_DYN0%RCP%KAP, Z_NHXD_T_T0)
  ! * compute NHX_s
  DO JLEV=1,NFLEVG
    DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
      Z_NHXS_T_T0(JROF,JLEV)=YDCPG_DYN0%NHX(JROF,JLEV)-Z_NHXD_T_T0(JROF,JLEV)
    ENDDO
  ENDDO
  ! ??? second method: compute ZNHXS_T0 via GPXX, ZNHXD_T0 is useless
  !     CALL GPXX as above
ENDIF

