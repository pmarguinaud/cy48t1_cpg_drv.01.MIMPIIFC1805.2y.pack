LLDER=.TRUE.
CALL GNHGRGW(YDGEOMETRY, NPROMA, NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, LLDER, Z_RT_T0, YDCPG_DYN0%RTL,           &
& YDCPG_DYN0%RTM, Z_DVER_T0, Z_DVERL_T0, Z_DVERM_T0, YDCPG_DYN0%XYB%LNPR, YDCPG_DYN0%XYB%ALPH, PUS=Z_US_T0,            &
& PVS=Z_VS_T0, PUS_L=Z_US_L_T0, PVS_L=Z_VS_L_T0, PUS_M=Z_US_M_T0, PVS_M=Z_VS_M_T0, POROGL=YDCPG_DYN0%OROGL,            &
& POROGM=YDCPG_DYN0%OROGM, POROGLM=P_OROGLM_T0, POROGLL=P_OROGLL_T0, POROGMM=P_OROGMM_T0, PGWFL=YDCPG_DYN0%GWFL,       &
& PGWFM=YDCPG_DYN0%GWFM, PGWHL=Z_GWHL_T0, PGWHM=Z_GWHM_T0, PLNPRL_DER=Z_XYBDER_LNPRL_T0, PLNPRM_DER=Z_XYBDER_LNPRM_T0, &
& PALPHL_DER=Z_XYBDER_ALPHL_T0, PALPHM_DER=Z_XYBDER_ALPHM_T0)

!*     3.1.19 COMPUTES 1/(R Tt [Delta log(prehyd)]) at t.

IF (.NOT.LGWADV) THEN
  DO JLEV=1,NFLEVG
    DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
      YDCPG_DYN0%RDPHI(JROF,JLEV)=1.0_JPRB/(YDCPG_DYN0%XYB%LNPR(JROF,JLEV)*Z_RT_T0(JROF,JLEV))
