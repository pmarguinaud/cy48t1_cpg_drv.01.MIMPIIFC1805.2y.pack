  ! ZR9T9 is R(t-dt)*Tt(t-dt); ZR0T9 is R(t)*Tt(t-dt) (for GPXX)
  Z_R9T_T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=YDVARS%T%T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)*YDCPG_DYN9%RCP%R(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)
  Z_R0T_T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=YDVARS%T%T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)*YDCPG_DYN0%RCP%R(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)

!*     3.2.6 COMPUTE QUANTITIES FOR NHQE MODEL.

  CALL GNHPRE(YDGEOMETRY, NPROMA, NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVARS%SPD%T9, YDCPG_DYN9%PREF, &
  & PKAP=YDCPG_DYN9%RCP%KAP, PNHPREF=YDCPG_DYN9%NHPREF)
  CALL GNHQE_PREH(YDGEOMETRY, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_DYN9%PRE, YDVARS%SPD%T9, YDCPG_DYN9%NHPREH&
  &     )

!*     3.2.8 COMPUTES THE GEOPOTENTIAL HEIGHT "gz" AND ITS GRADIENT.

  ! * "gz" at full levels and half levels.
  IF((LDLDIAB.OR.LSIMPH)) THEN
    YDCPG_DYN9%PHI(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,NFLEVG)=YDVARS%GEOMETRY%OROG%T0(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)
    CALL GPGEO(NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, NFLEVG, YDCPG_DYN9%PHI, YDCPG_DYN9%PHIF, &
    & Z_R9T_T9, Z_ONE_T0, YDCPG_DYN9%XYB%LNPR, YDCPG_DYN9%XYB%ALPH, YDGEOMETRY%YRVERT_GEOM)
  ENDIF

!*     3.2.10 COMPUTES HALF-LEVEL WINDS.

  ! Not needed if X-term and Z_term calculated FE.
  IF (LDGPXX .AND. .NOT.LVERTFE) THEN
    ! * same remark as for time t half-level winds.
    Z_UVH_WWI_T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=YDCPG_DYN0%UVH%WWI(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)
    CALL GPHLUV(YDGEOMETRY%YRDIMV, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVARS%U%T9, YDVARS%V%T9, &
    & PWWI=Z_UVH_WWI_T9, PUH=Z_UVH_UH_T9, PVH=Z_UVH_VH_T9)
  ENDIF

  LLDER=.FALSE.
  CALL GPUVS(NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, LLDER, YDVARS%U%T9, YDVARS%V%T9, &
  & Z_US_T9, Z_VS_T9)

!*     3.2.13 COMPUTES TERM "NHX".

  IF (LDGPXX) THEN
    ! * compute NHX_s = "wind shear" part of NHX
    CALL GPXX(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, Z_GPHL_T0, Z_GPHM_T0, YDCPG_DYN0%PHIFL, &
    & YDCPG_DYN0%PHIFM, YDCPG_DYN9%XYB%LNPR, Z_R0T_T9, YDVARS%U%T9, YDVARS%V%T9, Z_UVH_UH_T9,                         &
    & Z_UVH_VH_T9, Z_NHXS_T_T9)
    ! * compute NHX_d = "divergence" part of NHX
    CALL GNHQE_XXD(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDVARS%U%T9, YDVARS%V%T9, &
    & YDCPG_DYN9%XYB%LNPR, YDCPG_DYN9%XYB%RDELP, YDCPG_DYN9%XYB%ALPH, YDCPG_DYN9%XYB%RTGR, YDCPG_DYN9%PREL,  &
    & YDCPG_DYN9%PREM, YDCPG_DYN9%RCP%KAP, Z_NHXD_T_T9)
    ! * compute NHX
    DO JLEV=1,NFLEVG
      DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
        YDCPG_DYN9%NHX(JROF,JLEV)=Z_NHXS_T_T9(JROF,JLEV)+Z_NHXD_T_T9(JROF,JLEV)
      ENDDO
    ENDDO
  ELSE
    ! ??? we do not need separate contributions ZNHXS_T9 and ZNHXD_T9 in this case.
    YDCPG_DYN9%NHX(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=YDVARS%NHX%T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)
  ENDIF

!*     3.2.14 COMPUTES dver.

  DO JLEV=1,NFLEVG
    DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
      Z_DVER_T9(JROF,JLEV)=YDVARS%SVD%T9(JROF,JLEV)-YDCPG_DYN9%NHX(JROF,JLEV)
    ENDDO
  ENDDO

!*     3.2.16 COMPUTES term [gw].

  LLGDWI=.FALSE.
  IF (LGWADV.AND.LVFE_GW) THEN
    ! PGDW is required (at least for SL3TL)
    CALL GPGW(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, LDMPA, LLGDWI, YDCPG_DYN0%OROGL, &
    & YDCPG_DYN0%OROGM, YDCPG_DYN9%XYB%LNPR, YDCPG_DYN9%XYB%ALPH, Z_US_T9, Z_VS_T9, Z_R9T_T9,                  &
    & Z_DVER_T9, Z_GWHT_T9, YDCPG_DYN9%GWFT, PGDW=Z_GDW_T9)

    YDCPG_DYN9%GWT(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=YDCPG_DYN9%GWFT(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)
  ELSE
    CALL GPGW(YDGEOMETRY, NFLEVG, NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, LDMPA, LLGDWI, YDCPG_DYN0%OROGL, &
    & YDCPG_DYN0%OROGM, YDCPG_DYN9%XYB%LNPR, YDCPG_DYN9%XYB%ALPH, Z_US_T9, Z_VS_T9, Z_R9T_T9,                  &
    & Z_DVER_T9, Z_GWHT_T9, YDCPG_DYN9%GWFT)

    IF (LGWADV) YDCPG_DYN9%GWT(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1:NFLEVG)=Z_GWHT_T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,0:NFLEVG-1)
  ENDIF

  IF (LRDBBC) THEN
    ! PGWS contains (gw)_surf
    YDCPG_DYN0%GWS(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA)=Z_GWHT_T9(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,NFLEVG)
  ENDIF

ENDIF

!     ------------------------------------------------------------------

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CPG_GP_NHQE', 1, ZHOOK_HANDLE)
END SUBROUTINE CPG_GP_NHQE
