SUBROUTINE CPUTQY0 (YDCPG_DIM, PDT, PVAR, PTND)

USE PARKIND1, ONLY : JPRB, JPIM
USE CPG_DIM_TYPE_MOD, ONLY : CPG_DIM_TYPE

IMPLICIT NONE

TYPE (CPG_DIM_TYPE), INTENT(IN)  :: YDCPG_DIM
REAL (KIND=JPRB), INTENT (IN)    :: PDT
REAL (KIND=JPRB), INTENT (INOUT) :: PVAR (YDCPG_DIM%KLON,YDCPG_DIM%ILEVT1I:YDCPG_DIM%ILEVT1F)
REAL (KIND=JPRB), INTENT (IN)    :: PTND (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)

INTEGER (KIND=JPIM) :: JLEV, JROF

DO JLEV = 1, YDCPG_DIM%KFLEVG
  DO JROF = YDCPG_DIM%KIDIA, YDCPG_DIM%KFDIA
    PVAR (JROF, JLEV) = PVAR (JROF, JLEV) + PDT * PTND (JROF, JLEV)
  ENDDO
ENDDO

END SUBROUTINE


