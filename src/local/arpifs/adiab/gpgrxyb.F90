SUBROUTINE GPGRXYB (KPROMA,KD,KF,KFLEV,LDCOEF,YDVAB,PREL,PREM,PXYB,PXYBDER)

!**** *GPGRXYB* - Complement to routine "GPXYB".
!                 Computation of the horizontal gradient of quantities
!                 "alpha" and "delta" at model levels.

USE PARKIND1  , ONLY : JPIM, JPRB
USE YOMHOOK   , ONLY : LHOOK, DR_HOOK
USE YOMDYNA   , ONLY : YRDYNA
USE YOMCVER   , ONLY : LVERTFE 
USE YOMVERT   , ONLY : TVAB
USE INTDYN_MOD, ONLY : YYTXYB, YYTXYBDER

!     ------------------------------------------------------------------

IMPLICIT NONE

INTEGER(KIND=JPIM),INTENT(IN)    :: KPROMA 
INTEGER(KIND=JPIM),INTENT(IN)    :: KFLEV 
INTEGER(KIND=JPIM),INTENT(IN)    :: KD 
INTEGER(KIND=JPIM),INTENT(IN)    :: KF 
LOGICAL           ,INTENT(IN)    :: LDCOEF
TYPE(TVAB)        ,INTENT(IN)    :: YDVAB
REAL(KIND=JPRB)   ,INTENT(IN)    :: PREL(KPROMA) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PREM(KPROMA) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PXYB(KPROMA,KFLEV,YYTXYB%NDIM) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PXYBDER(KPROMA,KFLEV,YYTXYBDER%NDIM) 

#include "gpgrxyb_expl.intfb.h"

REAL (KIND=JPRB) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('GPGRXYB',0,ZHOOK_HANDLE)

CALL GPGRXYB_EXPL (LVERTFE, YRDYNA%NDLNPR, KPROMA,KD,KF,KFLEV,LDCOEF,YDVAB,PREL,PREM,&
                 & PXYB (:,:,YYTXYB%M_DELP), PXYB (:,:,YYTXYB%M_LNPR), PXYB (:,:,YYTXYB%M_RDELP), &
                 & PXYB (:,:,YYTXYB%M_ALPH), PXYB (:,:,YYTXYB%M_RTGR), PXYB (:,:,YYTXYB%M_RPRE),  &
                 & PXYB (:,:,YYTXYB%M_RPP), PXYBDER(:,:,YYTXYBDER%M_COEFD), PXYBDER(:,:,YYTXYBDER%M_LNPRL), &
                 & PXYBDER(:,:,YYTXYBDER%M_LNPRM), PXYBDER(:,:,YYTXYBDER%M_COEFA), &
                 & PXYBDER(:,:,YYTXYBDER%M_COEFAPL), PXYBDER(:,:,YYTXYBDER%M_ALPHPLL), &
                 & PXYBDER(:,:,YYTXYBDER%M_ALPHPLM), PXYBDER(:,:,YYTXYBDER%M_ALPHL), PXYBDER(:,:,YYTXYBDER%M_ALPHM))

IF (LHOOK) CALL DR_HOOK('GPGRXYB',1,ZHOOK_HANDLE)

END SUBROUTINE GPGRXYB
