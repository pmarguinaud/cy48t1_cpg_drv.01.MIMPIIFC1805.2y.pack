SUBROUTINE GPTF2_EXPL_2TL (YDGEOMETRY, KST, KEN, LDFSTEP, P0U, P0V, P9U, P9V)

USE PARKIND1, ONLY : JPIM, JPRB

USE GEOMETRY_MOD , ONLY : GEOMETRY

IMPLICIT NONE

TYPE(GEOMETRY)                                                                  ,INTENT(IN)               :: YDGEOMETRY
INTEGER(KIND=JPIM)                                                              ,INTENT(IN)               :: KST
INTEGER(KIND=JPIM)                                                              ,INTENT(IN)               :: KEN
LOGICAL                                                                         ,INTENT(IN)               :: LDFSTEP
REAL(KIND=JPRB)    ,DIMENSION(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG) ,INTENT(INOUT) ,OPTIONAL  :: P0U
REAL(KIND=JPRB)    ,DIMENSION(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG) ,INTENT(INOUT) ,OPTIONAL  :: P0V
REAL(KIND=JPRB)    ,DIMENSION(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG) ,INTENT(INOUT) ,OPTIONAL  :: P9U
REAL(KIND=JPRB)    ,DIMENSION(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG) ,INTENT(INOUT) ,OPTIONAL  :: P9V


INTEGER (KIND=JPIM) :: JK
INTEGER (KIND=JPIM) :: JL

IF(LDFSTEP) THEN
  DO JK=1,YDGEOMETRY%YRDIMV%NFLEVG
    DO JL=KST,KEN
      P9U(JL,JK) = P0U(JL,JK)
      P9V(JL,JK) = P0V(JL,JK)
    ENDDO
  ENDDO
ENDIF

END SUBROUTINE
