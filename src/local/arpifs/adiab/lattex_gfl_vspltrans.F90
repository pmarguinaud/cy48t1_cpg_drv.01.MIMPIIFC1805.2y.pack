SUBROUTINE LATTEX_GFL_VSPLTRANS (YDGEOMETRY, YDCPG_BNDS, YDML_GCONF, YDML_DYN, PB1, LDTDIABLIN)

USE PARKIND1, ONLY : JPIM, JPRB

USE GEOMETRY_MOD           , ONLY : GEOMETRY
USE CPG_OPTS_TYPE_MOD, ONLY : CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE MODEL_GENERAL_CONF_MOD , ONLY : MODEL_GENERAL_CONF_TYPE
USE MODEL_DYNAMICS_MOD     , ONLY : MODEL_DYNAMICS_TYPE


IMPLICIT NONE

TYPE(GEOMETRY)    ,INTENT(IN)    :: YDGEOMETRY
TYPE(CPG_BNDS_TYPE)   ,INTENT(IN)   :: YDCPG_BNDS
TYPE(MODEL_GENERAL_CONF_TYPE),INTENT(IN):: YDML_GCONF
TYPE(MODEL_DYNAMICS_TYPE),INTENT(IN):: YDML_DYN
REAL(KIND=JPRB)   ,INTENT(INOUT) :: PB1(YDGEOMETRY%YRDIM%NPROMA,YDML_DYN%YRPTRSLB1%NFLDSLB1)
LOGICAL :: LDTDIABLIN
INTEGER(KIND=JPIM) :: IPXSP

#include "vspltrans.intfb.h"

INTEGER (KIND=JPIM) :: IPX
INTEGER (KIND=JPIM) :: JGFL
INTEGER (KIND=JPIM) :: JLEV
INTEGER (KIND=JPIM) :: JROF

DO JGFL=1,YDML_GCONF%YGFL%NUMFLDS
  IF(YDML_GCONF%YGFL%YCOMP(JGFL)%LADV .AND. YDML_GCONF%YGFL%YCOMP(JGFL)%CSLINT=='LAITVSPCQM  ' ) THEN
    IPX=(YDML_GCONF%YGFL%YCOMP(JGFL)%MP_SL1-1)*(YDGEOMETRY%YRDIMV%NFLEN-YDGEOMETRY%YRDIMV%NFLSA+1)
    IPXSP=(YDML_GCONF%YGFL%YCOMP(JGFL)%MP_SPL-1)*(YDGEOMETRY%YRDIMV%NFLEN-YDGEOMETRY%YRDIMV%NFLSA+1)
    LDTDIABLIN=YDML_GCONF%YGFL%YCOMP(JGFL)%LTDIABLIN
    IF ((YDML_DYN%YRDYN%NSPLTHOI /= 0).OR.LDTDIABLIN) THEN
      DO JLEV=1,YDGEOMETRY%YRDIMV%NFLEVG
        DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
          PB1(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFLSPF9+IPXSP+JLEV-YDGEOMETRY%YRDIMV%NFLSA)=PB1(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFLF9+IPX+JLEV-YDGEOMETRY%YRDIMV%NFLSA)
        ENDDO
      ENDDO
      CALL VSPLTRANS(YDGEOMETRY%YRVSPLIP,YDGEOMETRY%YRDIM%NPROMA,YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIMV%NFLSA,YDGEOMETRY%YRDIMV%NFLEN,PB1(1,YDML_DYN%YRPTRSLB1%MSLB1GFLSPF9+IPXSP))
    ENDIF
    DO JLEV=1,YDGEOMETRY%YRDIMV%NFLEVG
      DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
        PB1(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFLSP9+IPXSP+JLEV-YDGEOMETRY%YRDIMV%NFLSA)=PB1(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFL9+IPX+JLEV-YDGEOMETRY%YRDIMV%NFLSA)
      ENDDO
    ENDDO
    CALL VSPLTRANS(YDGEOMETRY%YRVSPLIP,YDGEOMETRY%YRDIM%NPROMA,YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIMV%NFLSA,YDGEOMETRY%YRDIMV%NFLEN,PB1(1,YDML_DYN%YRPTRSLB1%MSLB1GFLSP9+IPXSP))
  ENDIF
ENDDO

END SUBROUTINE
