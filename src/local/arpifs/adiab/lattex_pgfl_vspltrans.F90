SUBROUTINE LATTEX_PGFL_VSPLTRANS (YDGEOMETRY, YDCPG_BNDS, YDML_GCONF, YDML_DYN, YDCPG_SL1)

USE PARKIND1              , ONLY : JPIM, JPRB
USE YOMHOOK               , ONLY : DR_HOOK, LHOOK

USE GEOMETRY_MOD          , ONLY : GEOMETRY
USE CPG_OPTS_TYPE_MOD     , ONLY : CPG_BNDS_TYPE
USE MODEL_GENERAL_CONF_MOD, ONLY : MODEL_GENERAL_CONF_TYPE
USE MODEL_DYNAMICS_MOD    , ONLY : MODEL_DYNAMICS_TYPE
USE CPG_TYPE_MOD          , ONLY : CPG_SL1_TYPE


IMPLICIT NONE

TYPE(GEOMETRY)                ,INTENT(IN)     :: YDGEOMETRY
TYPE(CPG_BNDS_TYPE)           ,INTENT(IN)     :: YDCPG_BNDS
TYPE(MODEL_GENERAL_CONF_TYPE) ,INTENT(IN)     :: YDML_GCONF
TYPE(MODEL_DYNAMICS_TYPE)     ,INTENT(IN)     :: YDML_DYN
TYPE(CPG_SL1_TYPE)            ,INTENT(INOUT)  :: YDCPG_SL1

#include "vspltrans.intfb.h"

LOGICAL :: LLTDIABLIN
INTEGER(KIND=JPIM) :: IPXSP
INTEGER (KIND=JPIM) :: IPX
INTEGER (KIND=JPIM) :: JGFL
INTEGER (KIND=JPIM) :: JLEV
INTEGER (KIND=JPIM) :: JROF

REAL(KIND=JPRB) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('LATTEX_PGFL_VSPLTRANS',0,ZHOOK_HANDLE)

DO JGFL=1,YDML_GCONF%YGFL%NUMFLDS
  IF(YDML_GCONF%YGFL%YCOMP(JGFL)%LADV .AND. YDML_GCONF%YGFL%YCOMP(JGFL)%CSLINT=='LAITVSPCQM  ' ) THEN
    IPX=(YDML_GCONF%YGFL%YCOMP(JGFL)%MP_SL1-1)*(YDGEOMETRY%YRDIMV%NFLEN-YDGEOMETRY%YRDIMV%NFLSA+1)
    IPXSP=(YDML_GCONF%YGFL%YCOMP(JGFL)%MP_SPL-1)*(YDGEOMETRY%YRDIMV%NFLEN-YDGEOMETRY%YRDIMV%NFLSA+1)
    LLTDIABLIN=YDML_GCONF%YGFL%YCOMP(JGFL)%LTDIABLIN
    IF ((YDML_DYN%YRDYN%NSPLTHOI /= 0).OR.LLTDIABLIN) THEN
      DO JLEV=1,YDGEOMETRY%YRDIMV%NFLEVG
        DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
          YDCPG_SL1%ZVIEW(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFLSPF9+IPXSP+JLEV-YDGEOMETRY%YRDIMV%NFLSA)=YDCPG_SL1%ZVIEW(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFLF9+IPX+JLEV-YDGEOMETRY%YRDIMV%NFLSA)
        ENDDO
      ENDDO
      CALL VSPLTRANS(YDGEOMETRY%YRVSPLIP,YDGEOMETRY%YRDIM%NPROMA,YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIMV%NFLSA,YDGEOMETRY%YRDIMV%NFLEN,YDCPG_SL1%ZVIEW(:,YDML_DYN%YRPTRSLB1%MSLB1GFLSPF9+IPXSP))
    ENDIF
    DO JLEV=1,YDGEOMETRY%YRDIMV%NFLEVG
      DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
        YDCPG_SL1%ZVIEW(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFLSP9+IPXSP+JLEV-YDGEOMETRY%YRDIMV%NFLSA)=YDCPG_SL1%ZVIEW(JROF,YDML_DYN%YRPTRSLB1%MSLB1GFL9+IPX+JLEV-YDGEOMETRY%YRDIMV%NFLSA)
      ENDDO
    ENDDO
    CALL VSPLTRANS(YDGEOMETRY%YRVSPLIP,YDGEOMETRY%YRDIM%NPROMA,YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIMV%NFLSA,YDGEOMETRY%YRDIMV%NFLEN,YDCPG_SL1%ZVIEW(:,YDML_DYN%YRPTRSLB1%MSLB1GFLSP9+IPXSP))
  ENDIF
ENDDO

IF (LHOOK) CALL DR_HOOK('LATTEX_PGFL_VSPLTRANS',1,ZHOOK_HANDLE)

END SUBROUTINE
