SUBROUTINE LAVABO_LAITVSPCQM_PART1 (YDGEOMETRY, YGFL, YDPTRSLB1, KST, KPROF, PB1)

USE PARKIND1, ONLY : JPIM, JPRB

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE YOM_YGFL     , ONLY : TYPE_GFLD
USE PTRSLB1      , ONLY : TPTRSLB1


IMPLICIT NONE

TYPE(GEOMETRY)    ,INTENT(IN)    :: YDGEOMETRY
TYPE(TYPE_GFLD)   ,INTENT(IN)    :: YGFL
TYPE(TPTRSLB1)    ,INTENT(IN)    :: YDPTRSLB1
INTEGER(KIND=JPIM),INTENT(IN)    :: KST
INTEGER(KIND=JPIM),INTENT(IN)    :: KPROF
REAL(KIND=JPRB)   ,INTENT(INOUT) :: PB1(YDGEOMETRY%YRDIM%NPROMA,YDPTRSLB1%NFLDSLB1)

INTEGER(KIND=JPIM) :: ISLB1GFL9
INTEGER (KIND=JPIM) :: JGFL


DO JGFL=1,YGFL%NUMFLDS
  IF( (YGFL%YCOMP(JGFL)%CSLINT /= 'LAITVSPCQM  ').AND.YGFL%YCOMP(JGFL)%LADV ) THEN
    ISLB1GFL9=YDPTRSLB1%MSLB1GFL9+(YGFL%YCOMP(JGFL)%MP_SL1-1)*(YDGEOMETRY%YRDIMV%NFLEN-YDGEOMETRY%YRDIMV%NFLSA+1)
    PB1(KST:KPROF,ISLB1GFL9)=0.0_JPRB
    PB1(KST:KPROF,ISLB1GFL9+YDGEOMETRY%YRDIMV%NFLEVG+1)=0.0_JPRB
  ENDIF
ENDDO

END SUBROUTINE
