SUBROUTINE APL_ARPEGE_SOIL_HYDRO (YDMF_PHYS_BASE_STATE, YDCPG_DIM, YDCPG_OPTS, YDMF_PHYS, YDMF_PHYS_SURF,     &
& YDMODEL, PCHROV, PFLU_NEIJ, PFLU_QSAT, PFLU_QSATS, PFLU_VEG, PGWDCS, PHQ, PHTR, PHU, PQV, PWFC, &
& PWLMX, PWWILT)

USE PARKIND1, ONLY : JPIM, JPRB

USE MF_PHYS_BASE_STATE_TYPE_MOD &
                       , ONLY : MF_PHYS_BASE_STATE_TYPE
USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE, CPG_OPTS_TYPE
USE MF_PHYS_TYPE_MOD   , ONLY : MF_PHYS_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY : MF_PHYS_SURF_TYPE
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE (MF_PHYS_BASE_STATE_TYPE), INTENT(IN)    :: YDMF_PHYS_BASE_STATE
TYPE(CPG_DIM_TYPE),             INTENT(IN)    :: YDCPG_DIM
TYPE(CPG_OPTS_TYPE),            INTENT(IN)    :: YDCPG_OPTS
TYPE(MF_PHYS_TYPE),             INTENT(INOUT) :: YDMF_PHYS
TYPE(MF_PHYS_SURF_TYPE),        INTENT(INOUT) :: YDMF_PHYS_SURF
TYPE(MODEL),                    INTENT(IN)    :: YDMODEL
REAL(KIND=JPRB),                INTENT(IN)    :: PCHROV(YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(IN)    :: PFLU_NEIJ (YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(IN)    :: PFLU_QSAT (YDCPG_DIM%KLON, 1:YDCPG_DIM%KFLEVG)
REAL(KIND=JPRB),                INTENT(IN)    :: PFLU_QSATS (YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(IN)    :: PFLU_VEG (YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(IN)    :: PGWDCS(YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(OUT)   :: PHQ(YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(OUT)   :: PHTR(YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(INOUT) :: PHU(YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(IN)    :: PQV(YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL(KIND=JPRB),                INTENT(IN)    :: PWFC(YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(OUT)   :: PWLMX(YDCPG_DIM%KLON)
REAL(KIND=JPRB),                INTENT(IN)    :: PWWILT(YDCPG_DIM%KLON)

#include "acveg.intfb.h"


! BILAN HYDRIQUE DU SOL (IF NOT SURFEX)
IF (YDMODEL%YRML_PHY_MF%YRPHY%LSOLV.AND.(.NOT.YDMODEL%YRML_PHY_MF%YRARPHY%LMSE)) THEN
  CALL ACVEG ( YDMODEL%YRML_PHY_MF%YRPHY, YDMODEL%YRML_PHY_MF%YRPHY1, YDCPG_DIM%KIDIA, YDCPG_DIM%KFDIA,                      &
  & YDCPG_DIM%KLON, YDCPG_DIM%KFLEVG, YDMF_PHYS%OUT%FRSO, PQV, PFLU_QSAT, YDMF_PHYS_BASE_STATE%T, YDMF_PHYS_SURF%GSD_VV%PD2, &
  & YDMF_PHYS_SURF%GSD_VF%PLSM, YDMF_PHYS_SURF%GSD_VV%PIVEG, YDMF_PHYS_SURF%GSD_VV%PLAI, PFLU_NEIJ,                          &
  & PFLU_VEG, YDMF_PHYS_SURF%GSD_VV%PRSMIN, PCHROV, PGWDCS, PWFC, YDMF_PHYS_BASE_STATE%YGSP_RR%FC,                           &
  & PWWILT, YDMF_PHYS_BASE_STATE%YGSP_SB%Q, PFLU_QSATS, PHQ, PHTR, PHU, YDMF_PHYS_SURF%GSD_VV%PHV,                           &
  & PWLMX)
ENDIF

END SUBROUTINE
