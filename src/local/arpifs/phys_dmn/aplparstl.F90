REAL(KIND=JPRB)      ,INTENT(OUT)   :: PFRSO(KLON,0:KLEV,KSGST+1) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PFRTH(KLON,0:KLEV,KSGST+1) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRCU(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRCV(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRDU(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRDV(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRTU(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRTV(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRMU(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PSTRMV(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PFRMH(KLON,0:KLEV) 
REAL(KIND=JPRB)      ,INTENT(OUT)   :: PTRIGCONV5(KLON) 
TYPE (TRAJ_PHYS_TYPE),INTENT(IN)    :: PTRAJ_PHYS



!     ------------------------------------------------------------------

! - 2D (0:KLEV) .

! ZKTROV     : VERTICAL EXCHANGE COEFFICIENT OF T AND Q IN KG/(M*M*S).
! ZKUROV     : VERTICAL EXCHANGE COEFFICIENT OF U AND V IN KG/(M*M*S).
! ZXTROV     : "ANTI-FIBRILLATION" MULTIPLICATOR OF PKTROV.
! ZXUROV     : "ANTI-FIBRILLATION" MULTIPLICATOR OF PKUROV.
! ZFPLC      : SUM OF CONVECTIVE PRECIPITATION AS RAIN AND AS SNOW.

!  The same but for trajectory
!  ---------------------------
! ZFPLC5     : SUM OF CONVECTIVE PRECIPITATION AS RAIN AND AS SNOW.

! - 1D (DIAGNOSTIC) .

! ZCDROV     : PCD NORMALIZED BY WIND SPEED TIMES DENSITY.
! ZCHROV     : PCH NORMALIZED BY WIND SPEED TIMES DENSITY.
! ZXDROV     : "ANTI-FIBRILLATION" MULTIPLICATOR OF PCDROV.
! ZXHROV     : "ANTI-FIBRILLATION" MULTIPLICATOR OF PCHROV.
! ZNEIJ      : FRACTION OF SOIL COVERED BY SNOW.
! ZFCLL      : LATENT HEAT FLUX OVER LIQUID WATER (OR WET SOIL).
! ZFCLN      : LATENT HEAT FLUX OVER SNOW (OR ICE).
! ZFCS       : SENSIBLE HEAT FLUX AT SURFACE LEVEL.
! ZFEVL      : WATER VAPOR FLUX OVER LIQUID WATER (OR WET SOIL).
! ZFEVN      : WATER VAPOR FLUX OVER SNOW (OR ICE).
! ZLHS       : LATENT HEAT AT THE SURFACE.
! ZALB       : MODEL SURFACE SHORT ALBEDO.

! - 1D (GEOGRAPHIC) .

! ZGZ0       : GRAVITY * ROUGHNESS LENGTH.

REAL(KIND=JPRB) :: ZXTROV(KLON,0:KLEV),ZXUROV(KLON,0:KLEV)
REAL(KIND=JPRB) :: ZKTROV(KLON,0:KLEV),ZKUROV(KLON,0:KLEV)
REAL(KIND=JPRB) :: ZFPLC (KLON,0:KLEV)

REAL(KIND=JPRB) :: ZXDROV(KLON),ZXHROV(KLON),ZCDROV(KLON),ZCHROV(KLON),&
 & ZGZ0(KLON),ZNEIJ(KLON),&
 & ZLHS(KLON),ZFEVL(KLON),ZFEVN(KLON),&
 & ZFCLL(KLON),ZFCLN(KLON),ZFCS(KLON),&
 & ZALB(KLON)

REAL(KIND=JPRB) :: ZNTS(KLON),ZTSDUM(KLON)
REAL(KIND=JPRB) :: ZERO1(KLON,KLEV)

REAL(KIND=JPRB) :: ZFPLC5 (KLON,0:KLEV)

INTEGER(KIND=JPIM) :: IJN, JLEV, JLON

REAL(KIND=JPRB)   :: ZDPTS(KLON) 
REAL(KIND=JPRB) :: ZPR (KLON,KLEV),ZPR5(KLON,KLEV)

!           TRAJECTORY (For diffusion !) local VARIABLES
!           ----------------------------
INTEGER(KIND=JPIM) :: IFIELDS,IPCT

REAL(KIND=JPRB) :: Z0CR, Z0CRIW
REAL(KIND=JPRB) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------
#include "acconvstl.intfb.h"
#include "acconvtl.intfb.h"
#include "acdifsptl.intfb.h"
#include "acdragltl.intfb.h"
#include "acdrmetl.intfb.h"
#include "acqwlsrtl.intfb.h"
#include "acradstl.intfb.h"
#include "actsectl.intfb.h"
#include "rdphtrajtm_nl.intfb.h"
#include "aclspstl.intfb.h"


!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('APLPARSTL',0,ZHOOK_HANDLE)
ASSOCIATE(YDSTA=>YDGEOMETRY%YRSTA, YDVAB=>YDGEOMETRY%YRVAB,    YDPHY=>YDML_PHY_MF%YRPHY,   YDTOPH=>YDML_PHY_MF%YRTOPH,&
& YDSIMPHL=>YDML_PHY_MF%YRSIMPHL,YDPHY1=>YDML_PHY_MF%YRPHY1,YDPHY0=>YDML_PHY_MF%YRPHY0)

ASSOCIATE(TEQK=>YDPHY0%TEQK,   ALCRIN=>YDPHY1%ALCRIN, ALRCN2=>YDPHY1%ALRCN2, ALRCN1=>YDPHY1%ALRCN1,                   &
& WCRIN=>YDPHY1%WCRIN,   RMESOT=>YDTOPH%RMESOT, RMESOU=>YDTOPH%RMESOU,   LVDIFSPNL=>YDSIMPHL%LVDIFSPNL,               &
& LPROCLDTL=>YDSIMPHL%LPROCLDTL, LSTRASPN=>YDSIMPHL%LSTRASPN,   LCVRASP=>YDSIMPHL%LCVRASP, LRAYSP=>YDSIMPHL%LRAYSP,   &
& LCVRASBM=>YDSIMPHL%LCVRASBM, LSTRASP=>YDSIMPHL%LSTRASP,   LRRMESSP=>YDSIMPHL%LRRMESSP, LGWDSP=>YDSIMPHL%LGWDSP,     &
& LGWDSPNL=>YDSIMPHL%LGWDSPNL,LVDIFSP=>YDSIMPHL%LVDIFSP,   LNEIGE=>YDPHY%LNEIGE, STTEM=>YDSTA%STTEM)
!     ------------------------------------------------------------------

!*
!**
!     -----------------------------------------------------------------
!     I - SOME INITIALIZATION AND SET VARIABLES TO ZERO IN THE CASE OF
!         NON-CALLING PARAMETRIZATIONS.
!     ------------------------------------------------------------------

IJN=KLON

PDIFCQ5(:,:) = 0.0_JPRB
PDIFCQ(:,:) = 0.0_JPRB
PDIFCS5(:,:) = 0.0_JPRB
PDIFCS(:,:) = 0.0_JPRB
PDIFTQ5(:,:) = 0.0_JPRB
PDIFTQ(:,:) = 0.0_JPRB
PDIFTS5(:,:) = 0.0_JPRB
PDIFTS(:,:) = 0.0_JPRB
PFPLCL5(:,:) = 0.0_JPRB
PFPLCL(:,:) = 0.0_JPRB
PFPLCN5(:,:) = 0.0_JPRB
PFPLCN(:,:) = 0.0_JPRB
PFPLSL5(:,:) = 0.0_JPRB
PFPLSL(:,:) = 0.0_JPRB
PFPLSN5(:,:) = 0.0_JPRB
PFPLSN(:,:) = 0.0_JPRB
PFCCQN5(:,:) = 0.0_JPRB
PFCCQN(:,:) = 0.0_JPRB
PFCCQL5(:,:) = 0.0_JPRB
PFCCQL(:,:) = 0.0_JPRB
PFCSQN5(:,:) = 0.0_JPRB
PFCSQN(:,:) = 0.0_JPRB
PFCSQL5(:,:) = 0.0_JPRB
PFCSQL(:,:) = 0.0_JPRB
PSTRCU5(:,:) = 0.0_JPRB
PSTRCU(:,:) = 0.0_JPRB
PSTRCV5(:,:) = 0.0_JPRB
PSTRCV(:,:) = 0.0_JPRB
PSTRDU5(:,:) = 0.0_JPRB
PSTRDU(:,:) = 0.0_JPRB
PSTRDV5(:,:) = 0.0_JPRB
PSTRDV(:,:) = 0.0_JPRB
PSTRTU5(:,:) = 0.0_JPRB
PSTRTU(:,:) = 0.0_JPRB
PSTRTV5(:,:) = 0.0_JPRB
PSTRTV(:,:) = 0.0_JPRB
PSTRMU5(:,:) = 0.0_JPRB
PSTRMU(:,:) = 0.0_JPRB
PSTRMV5(:,:) = 0.0_JPRB
PSTRMV(:,:) = 0.0_JPRB
PFRMH5 (:,:) = 0.0_JPRB
PFRMH (:,:) = 0.0_JPRB

PFRSO5(:,:,:) = 0.0_JPRB
PFRSO(:,:,:) = 0.0_JPRB
PFRTH5(:,:,:) = 0.0_JPRB
PFRTH(:,:,:) = 0.0_JPRB

ZFCLL(:) = 0.0_JPRB
ZFCLN(:) = 0.0_JPRB
ZFCS(:)  = 0.0_JPRB
ZFEVL(:) = 0.0_JPRB
ZFEVN(:) = 0.0_JPRB
ZLHS(:)  = 0.0_JPRB

!*

!*
!     -------------------------------------------------------------------
!     II - COMPUTATION OF FRACTION OF SOIL COVERED BY SNOW AND ROUGHNESS
!     -------------------------------------------------------------------
DO JLON=KIDIA,KFDIA

!     COMPUTATIONS CONCERNING THE PRESENCE OR ABSENCE OF SNOW ON GROUND.

  Z0CR=RG*ALRCN1
  Z0CRIW=1.0_JPRB/(RG*ALRCN2)

  IF (LNEIGE) THEN
    ZNEIJ(JLON)=PLSM(JLON)*PSNS(JLON)/(PSNS(JLON)+WCRIN)
    ZGZ0(JLON)=PGZ0F(JLON)+(Z0CR-PGZ0F(JLON))*PLSM(JLON)&
     & *PSNS(JLON)/(PSNS(JLON)+WCRIN+Z0CRIW*PGZ0F(JLON))  
  ELSE
    ZNEIJ(JLON)=0.0_JPRB
    ZGZ0(JLON)=PGZ0F(JLON)
  ENDIF

ENDDO

!*
!     ------------------------------------------------------------------
!     III - COMPUTATION OF VERTICAL TURBULENT EXCHANGE COEFFICIENTS
!           AND SURFACE EXCHANGE COEFFICIENTS AND COMPUTATION OF
!           VERTICAL TURBULENT DIFFUSION
!     ------------------------------------------------------------------
IF ( LVDIFSP) THEN
  DO JLON=KIDIA, KFDIA

    ZNTS(JLON)=PTS1(JLON)

  ENDDO

  IF (LVDIFSPNL) THEN
  
     !------------------------------------------------------
     ! Read in diffusion coefficients from non linear model.
     !------------------------------------------------------

     IFIELDS=NG3DT95*KLEV+NG3PR95*(KLEV+1)+NG2DSF5 
     IPCT = 0_JPIM ! <== IPCT is not used in RDPHTRAJTM
     
      CALL RDPHTRAJTM_NL(YDGEOMETRY,YDSIMPHL,KIDIA,KFDIA,PTRAJ_PHYS,&
                & PKTROV5,PKUROV5,&
                & PCDROV5,PCHROV5,PTRAJGWD5,&
                & PZQL5,PZQI5,PZQR5,PZQS5,PZLIQCVPPKF,PZNEBSCVPPKF)
      PXDROV5(:)   = 1.0_JPRB
      PXHROV5(:)   = 1.0_JPRB
      ZXDROV(:)    = 0.0_JPRB
      ZCDROV(:)    = 0.0_JPRB
      ZXHROV(:)    = 0.0_JPRB
      ZCHROV(:)    = 0.0_JPRB
      PXTROV5(:,:) = 1.0_JPRB
      PXUROV5(:,:) = 1.0_JPRB
      ZKTROV(:,:)  = 0.0_JPRB
      ZKUROV(:,:)  = 0.0_JPRB
      ZXTROV(:,:)  = 0.0_JPRB
      ZXUROV(:,:)  = 0.0_JPRB
      PTRIGCONV5(:)= 9999999.9999_JPRB

  ELSE 
     !---------------------------------------------------------
     ! Compute diffusion coefficients using simplified physics.
     !---------------------------------------------------------

     CALL ACTSECTL(YDML_PHY_MF,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
      & PAPHI5,PAPHIF5,PAPRS5,PAPRSF5,PQ5,PT5,PU5,PV5,&
      & PAPHI,PAPHIF,PAPRS,PAPRSF,PQ,PT,PU,PV,&
      & PQS,PTS,ZGZ0,&
      & PKTROV5,PKUROV5,PXTROV5,PXUROV5,&
      & ZKTROV,ZKUROV,ZXTROV,ZXUROV,&
      & PCDROV5,PCHROV5,PXDROV5,PXHROV5,&
      & ZCDROV,ZCHROV,ZXDROV,ZXHROV)

  ENDIF

  CALL ACDIFSPTL(YDML_PHY_MF,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
   & PAPHI5,PAPHIF5,PDELP5,PRDELP5,PT5,PQ5,&
   & PU5,PV5,PKTROV5,PKUROV5,PXTROV5,PXUROV5,&
   & PAPHI,PAPHIF,PDELP,PRDELP,PT,PQ,&
   & PU,PV,ZKTROV,ZKUROV,ZXTROV,ZXUROV,&
   & PCDROV5,PCHROV5,PXDROV5,PXHROV5,&
   & ZCDROV,ZCHROV,ZXDROV,ZXHROV,&
   & PLSM,ZNEIJ,PQS,PTS,ZNTS,PTS1,PQS1,&
   & PDIFTQ5,PDIFTS5,PSTRTU5,PSTRTV5,&
   & PDIFTQ,PDIFTS,PSTRTU,PSTRTV,&
   & ZFCLL,ZFCLN,ZFCS,ZFEVL,ZFEVN,ZLHS)
ENDIF

!*
!     ------------------------------------------------------------------
!     IV - COMPUTATION OF OROGRAPHIC GRAVITY WAVE DRAG
!     ------------------------------------------------------------------
IF ( LGWDSP ) THEN

  IF (LGWDSPNL) THEN

     !------------------------------------------------------
     ! Read in coefficients PTRAJGWD5 from non linear model.
     !------------------------------------------------------


     IFIELDS=NG3DT95*KLEV+NG3PR95*(KLEV+1)+NG2DSF5
     IPCT = 0_JPIM ! <== IPCT is not used in RDPHTRAJTM

     CALL RDPHTRAJTM_NL(YDGEOMETRY,YDSIMPHL,KIDIA,KFDIA,PTRAJ_PHYS,&
            & PKTROV5,PKUROV5,&
            & PCDROV5,PCHROV5,PTRAJGWD5,&
            & PZQL5,PZQI5,PZQR5,PZQS5,PZLIQCVPPKF,PZNEBSCVPPKF)
     
  ENDIF

  CALL ACDRAGLTL(YDML_PHY_MF,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
   & PAPHI5,PAPHIF5,PAPRS5,PAPRSF5,PDELP5,PRDELP5,&
   & PU5,PV5,PT5,PQ5,&
   & PAPHI,PAPHIF,PAPRS,PAPRSF,PDELP,PRDELP,&
   & PU,PV,PT,PQ,&
   & PGETRL,PVRLAN,PVRLDI,PTS,PQS,&
   & PSTRDU5,PSTRDV5,&
   & PSTRDU,PSTRDV,PTRAJGWD5)
ENDIF

!*
!     ------------------------------------------------------------------
!     V - COMPUTING CONVECTIVE PRECIPITATIONS, DEEP CONVECTIVE SCHEME
!     ------------------------------------------------------------------

IF ( LVDIFSP.AND.LCVRASP ) THEN
!        THE DYNAMIC TENDENCY OF Q IS MULTIPLIED BY CORRECTION FACTOR
!        OF LOCAL RESOLUTION OF THE MODEL AND BY ARGUMENT OF THE
!        EVAPORATION OF THE SURFACE

  DO JLEV=KTDIA,KLEV
    DO JLON=KIDIA,KFDIA
      PCVGQ5(JLON,JLEV)=PCVGQ5(JLON,JLEV)/(1.0_JPRB+PGM(JLON)*TEQK)&
       & -RG*PRDELP5(JLON,JLEV)&
       & *(PDIFTQ5(JLON,JLEV)-PDIFTQ5(JLON,JLEV-1))  
      PCVGQ(JLON,JLEV)=PCVGQ(JLON,JLEV)/(1.0_JPRB+PGM(JLON)*TEQK)&
       & -RG*(PDIFTQ5(JLON,JLEV)-PDIFTQ5(JLON,JLEV-1))&
       & *PRDELP(JLON,JLEV)&
       & -RG*PRDELP5(JLON,JLEV)*PDIFTQ(JLON,JLEV)&
       & +RG*PRDELP5(JLON,JLEV)*PDIFTQ(JLON,JLEV-1)  
    ENDDO
  ENDDO
ENDIF

IF ( LCVRASP ) THEN
  CALL ACCONVTL(YDML_PHY_MF,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
   & PAPHIF5,PAPRS5,PAPRSF5,PCVGQ5,&
   & PDELP5,PQ5,PRDELP5,PT5,PU5,PV5,&
   & PAPHIF,PAPRS,PAPRSF,PCVGQ,&
   & PDELP,PQ,PRDELP,PT,PU,PV,&
   & PTS,&
   & PDIFCQ5,PDIFCS5,PFPLCL5,PFPLCN5,&
   & PSTRCU5,PSTRCV5,&
   & PDIFCQ,PDIFCS,PFPLCL,PFPLCN,PSTRCU,PSTRCV)
ENDIF

IF (LCVRASBM)  THEN 
  ZERO1(:,:)=0.0
  CALL ACCONVSTL(YDML_PHY_MF%YRPHY2,YDSIMPHL,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
   & PAPHIF5,PAPRS5,PAPRSF5,PCVGQ5,&
   & PDELP5,PQ5,PRDELP5,PT5,PU5,PV5,&
   & PAPHIF,PAPRS,PAPRSF,PCVGQ,&
   & ZERO1,PQ,ZERO1,PT,PU,PV,&
   & PTS,ZTSDUM,&
   & PDIFCQ5,PDIFCS5,PFPLCL5,PFPLCN5,PSTRCU5,PSTRCV5,&
   & PDIFCQ,PDIFCS,PFPLCL,PFPLCN,PSTRCU,PSTRCV,PTRIGCONV5)
ENDIF
!     ------------------------------------------------------------------
!     VI - COMPUTATION OF WET-BULB CHARACTERISTICS AND STRATIFORM
!          PRECIPITATION FLUXES
!     ------------------------------------------------------------------
IF ( LSTRASP ) THEN
  CALL ACQWLSRTL(YDML_PHY_MF,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
   & PQ5,PT5,PAPRSF5,PDELP5,&
   & PQ,PT,PAPRSF,PDELP,&
   & PFPLSL5,PFPLSN5,&
   & PFPLSL,PFPLSN)
ENDIF

IF(LSTRASPN) THEN 
  ZPR5(:,:)=RD+(RV-RD)*PQ5(:,:)
  ZPR(KIDIA:KFDIA,KTDIA:KLEV)=0.0_JPRB ! For time beeing but very dirty 
 
  IF (LPROCLDTL) THEN
     PZQL5(:,:)=PQL5(:,:)
     PZQI5(:,:)=PQI5(:,:)
  ENDIF

  CALL ACLSPSTL(YDGEOMETRY,YDML_PHY_MF,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
   &PQ5,PZQL5,PZQI5,PZQR5,PZQS5,PZLIQCVPPKF,PZNEBSCVPPKF,&
   &PT5,PDELP5,PAPRSF5,PCP5,ZPR5,PAPHI5,PTS,PGM,&
   &PQ,PQL,PQI,PT,PDELP,PAPRSF,PCP,ZPR,PAPHI,ZDPTS,PLSM,&
   &PFCSQL5,PFCSQN5,PFPLSL5,PFPLSN5,&
   &PFCSQL,PFCSQN,PFPLSL,PFPLSN)
ENDIF


!*
!     -------------------------------------------------------------------
!     VII - COMPUTATION OF RADIATIVE FLUXES
!     -------------------------------------------------------------------

IF (LRAYSP) THEN

!        ALBEDO AND EMISSIVITY WITH SNOW

  DO JLON=KIDIA,KFDIA
    ZALB(JLON)=PALBF(JLON)-ZNEIJ(JLON)*(PALBF(JLON)-MAX(PALBF(JLON),ALCRIN))
  ENDDO

  DO JLEV=KTDIA-1,KLEV
    DO JLON=KIDIA,KFDIA
      ZFPLC5(JLON,JLEV)=PFPLCL5(JLON,JLEV)+PFPLCN5(JLON,JLEV)
      ZFPLC(JLON,JLEV)=PFPLCL(JLON,JLEV)+PFPLCN(JLON,JLEV)
    ENDDO
  ENDDO

!        COMPUTATION OF THE FLUXES

  CALL ACRADSTL(YDVAB,YDML_PHY_MF,KIDIA,KFDIA,KLON,KTDIA,KLEV,IJN,&
   & PT5,PAPRSF5,PQ5,PDELP5,PRDELP5,ZFPLC5,&
   & PT,PAPRSF,PQ,PDELP,PRDELP,ZFPLC,&
   & ZALB,PMU0,PTS,&
   & PAC,PCOR,&
   & PRAB3C,PRAB3N,PRAB4C,PRAB4N,PRAB6C,PRAB6N,&
   & PRAT1C,PRAT1N,PRAT2C,PRAT2N,PRAT3C,PRAT3N,&
   & PRAT4C,PRAT4N,PRAT5C,PRAT5N,&
   & PFRSO5,PFRTH5,&
   & PFRSO,PFRTH)
ENDIF

!*
!     ------------------------------------------------------------------
!     VIII - COMPUTES THE MOMENTUM FLUX DUE TO A MESOSPHERIC DRAG AND 
!            THE HEAT FLUX CORRESPONDING TO A RELAXATION OF TEMPERATURE 
!            TOWARDS THE STANDARD ATMOSPHERE
!     ------------------------------------------------------------------

IF ( LRRMESSP ) THEN
  CALL ACDRMETL(YDML_PHY_MF%YRPHY2,KIDIA,KFDIA,KLON,KTDIA,KLEV,&
   & PCP5,PDELP5,PT5,PU5,PV5,&
   & PCP,PDELP,PT,PU,PV,&
   & PFRMH5,PSTRMU5,PSTRMV5,&
   & PFRMH,PSTRMU,PSTRMV,&
   & RMESOT,RMESOU,STTEM)
ENDIF

!*
!     ------------------------------------------------------------------
!     IX - COMPUTATION OF THE FLUXES OF ENTHALPY AND SENSIBLE HEAT
!          (THE FUNCTION OF THE FLUXES OF PRECIPITATIONS AND
!           CONDENSATION)
!     ------------------------------------------------------------------

!      - IF IT IS NOT LIQUID WATER, THEN
!                   THE FLUX OF CONDENSATION = THE FLUX OF PRECIPITATION

IF (.NOT.LSTRASPN) THEN
  DO JLEV = KTDIA, KLEV
    DO JLON = KIDIA, KFDIA
      PFCCQL5(JLON,JLEV) = PFPLCL5(JLON,JLEV)
      PFCCQL(JLON,JLEV) = PFPLCL(JLON,JLEV)

      PFCCQN5(JLON,JLEV) = PFPLCN5(JLON,JLEV)
      PFCCQN(JLON,JLEV) = PFPLCN(JLON,JLEV)

      PFCSQL5(JLON,JLEV) = PFPLSL5(JLON,JLEV)
      PFCSQL(JLON,JLEV) = PFPLSL(JLON,JLEV)

      PFCSQN5(JLON,JLEV) = PFPLSN5(JLON,JLEV)
      PFCSQN(JLON,JLEV) = PFPLSN(JLON,JLEV)
    ENDDO
  ENDDO
ENDIF

!     ------------------------------------------------------------------
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('APLPARSTL',1,ZHOOK_HANDLE)
END SUBROUTINE APLPARSTL
