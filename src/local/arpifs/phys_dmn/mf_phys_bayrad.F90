SUBROUTINE MF_PHYS_BAYRAD (YDAPLPAR_TMP, YDVARS, YDMODEL, LDAROME)

USE PARKIND1, ONLY : JPIM, JPRB

USE MF_PHYS_TYPE_MOD   , ONLY : APLPAR_TMP_TYPE
USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(APLPAR_TMP_TYPE),INTENT(INOUT) :: YDAPLPAR_TMP
TYPE(FIELD_VARIABLES),INTENT(INOUT) :: YDVARS
TYPE(MODEL)       ,INTENT(INOUT) :: YDMODEL
LOGICAL, INTENT (IN) :: LDAROME




!--------------------------------------------------------------------
! BAYRAD
! Fill convective hydrometeors mixing ratio in GFL
!--------------------------------------------------------------------
IF((.NOT.YDMODEL%YRML_PHY_MF%YRPHY%LGPCMT).AND.(.NOT.LDAROME)) THEN
   IF(YDMODEL%YRML_GCONF%YGFL%YRCONV%LACTIVE) THEN
     YDVARS%RCONV%T1(:,:) = YDAPLPAR_TMP%BAY%QRCONV(:,:)
   ENDIF
   IF(YDMODEL%YRML_GCONF%YGFL%YSCONV%LACTIVE) THEN
     YDVARS%SCONV%T1(:,:) = YDAPLPAR_TMP%BAY%QSCONV(:,:)
   ENDIF
ENDIF



END SUBROUTINE
