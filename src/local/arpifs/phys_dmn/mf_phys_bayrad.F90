SUBROUTINE MF_PHYS_BAYRAD (YDCPG_BNDS, YDCPG_OPTS, PBAY_QRCONV, PBAY_QSCONV, PRCONV_T1, PSCONV_T1, YDMODEL)

USE YOMHOOK, ONLY : LHOOK, DR_HOOK
USE PARKIND1, ONLY : JPIM, JPRB

USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL
USE CPG_OPTS_TYPE_MOD   , ONLY : CPG_BNDS_TYPE, CPG_OPTS_TYPE


IMPLICIT NONE


TYPE (CPG_BNDS_TYPE),INTENT (IN)  :: YDCPG_BNDS
TYPE (CPG_OPTS_TYPE),INTENT (IN)  :: YDCPG_OPTS
REAL (KIND=JPRB),    INTENT (IN)  :: PBAY_QRCONV (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB),    INTENT (IN)  :: PBAY_QSCONV (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB),    INTENT (OUT) :: PRCONV_T1 (YDCPG_OPTS%KLON,YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB),    INTENT (OUT) :: PSCONV_T1 (YDCPG_OPTS%KLON,YDCPG_OPTS%KFLEVG)
TYPE (MODEL),        INTENT (IN)  :: YDMODEL

REAL (KIND=JPRB) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('MF_PHYS_BAYRAD', 0, ZHOOK_HANDLE)

!--------------------------------------------------------------------
! BAYRAD
! Fill convective hydrometeors mixing ratio in GFL
!--------------------------------------------------------------------
IF((.NOT.YDMODEL%YRML_PHY_MF%YRPHY%LGPCMT).AND.(.NOT.YDCPG_OPTS%LAROME)) THEN
   IF(YDMODEL%YRML_GCONF%YGFL%YRCONV%LACTIVE) THEN
     PRCONV_T1(:,:) = PBAY_QRCONV(:,:)
   ENDIF
   IF(YDMODEL%YRML_GCONF%YGFL%YSCONV%LACTIVE) THEN
     PSCONV_T1(:,:) = PBAY_QSCONV(:,:)
   ENDIF
ENDIF

IF (LHOOK) CALL DR_HOOK ('MF_PHYS_BAYRAD', 1, ZHOOK_HANDLE)

END SUBROUTINE MF_PHYS_BAYRAD
