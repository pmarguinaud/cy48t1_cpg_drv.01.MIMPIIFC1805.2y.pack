SUBROUTINE MF_PHYS_BAYRAD (YDCPG_DIM, YDCPG_OPTS, PBAY_QRCONV, PBAY_QSCONV, PRCONV_T1, PSCONV_T1, YDMODEL, LDAROME)

USE PARKIND1, ONLY : JPIM, JPRB

USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL
USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE, CPG_OPTS_TYPE


IMPLICIT NONE


TYPE (CPG_DIM_TYPE), INTENT(IN) :: YDCPG_DIM
TYPE (CPG_OPTS_TYPE),INTENT(IN) :: YDCPG_OPTS
REAL (KIND=JPRB), INTENT(IN) :: PBAY_QRCONV (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT(IN) :: PBAY_QSCONV (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KFLEVG)

REAL (KIND=JPRB) :: PRCONV_T1 (YDCPG_OPTS%KLON,YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB) :: PSCONV_T1 (YDCPG_OPTS%KLON,YDCPG_OPTS%KFLEVG)
TYPE(MODEL)       ,INTENT(IN)    :: YDMODEL
LOGICAL, INTENT (IN) :: LDAROME




!--------------------------------------------------------------------
! BAYRAD
! Fill convective hydrometeors mixing ratio in GFL
!--------------------------------------------------------------------
IF((.NOT.YDMODEL%YRML_PHY_MF%YRPHY%LGPCMT).AND.(.NOT.LDAROME)) THEN
   IF(YDMODEL%YRML_GCONF%YGFL%YRCONV%LACTIVE) THEN
     PRCONV_T1(:,:) = PBAY_QRCONV(:,:)
   ENDIF
   IF(YDMODEL%YRML_GCONF%YGFL%YSCONV%LACTIVE) THEN
     PSCONV_T1(:,:) = PBAY_QSCONV(:,:)
   ENDIF
ENDIF



END SUBROUTINE
