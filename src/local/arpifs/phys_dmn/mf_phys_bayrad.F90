SUBROUTINE MF_PHYS_BAYRAD (YDCPG_DIM, PBAY_QRCONV, PBAY_QSCONV, YDVARS, YDMODEL, LDAROME)

USE PARKIND1, ONLY : JPIM, JPRB

USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL
USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE


IMPLICIT NONE


TYPE (CPG_DIM_TYPE), INTENT(IN) :: YDCPG_DIM
REAL (KIND=JPRB), INTENT(IN) :: PBAY_QRCONV (YDCPG_DIM%KLON, 1:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT(IN) :: PBAY_QSCONV (YDCPG_DIM%KLON, 1:YDCPG_DIM%KFLEVG)
TYPE(FIELD_VARIABLES),INTENT(INOUT) :: YDVARS
TYPE(MODEL)       ,INTENT(INOUT) :: YDMODEL
LOGICAL, INTENT (IN) :: LDAROME




!--------------------------------------------------------------------
! BAYRAD
! Fill convective hydrometeors mixing ratio in GFL
!--------------------------------------------------------------------
IF((.NOT.YDMODEL%YRML_PHY_MF%YRPHY%LGPCMT).AND.(.NOT.LDAROME)) THEN
   IF(YDMODEL%YRML_GCONF%YGFL%YRCONV%LACTIVE) THEN
     YDVARS%RCONV%T1(:,:) = PBAY_QRCONV(:,:)
   ENDIF
   IF(YDMODEL%YRML_GCONF%YGFL%YSCONV%LACTIVE) THEN
     YDVARS%SCONV%T1(:,:) = PBAY_QSCONV(:,:)
   ENDIF
ENDIF



END SUBROUTINE
