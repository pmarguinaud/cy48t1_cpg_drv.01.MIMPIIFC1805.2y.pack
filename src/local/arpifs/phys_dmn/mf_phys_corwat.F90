SUBROUTINE MF_PHYS_CORWAT (YDCPG_DIM, YDMF_PHYS, YDMF_PHYS_SURF, KST, KEND, KFLEVG)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE
USE MF_PHYS_TYPE_MOD   , ONLY : MF_PHYS_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY : MF_PHYS_SURF_TYPE


IMPLICIT NONE

TYPE(MF_PHYS_TYPE),INTENT(INOUT) :: YDMF_PHYS
TYPE(CPG_DIM_TYPE),INTENT(IN)    :: YDCPG_DIM
TYPE(MF_PHYS_SURF_TYPE),INTENT(INOUT) :: YDMF_PHYS_SURF
INTEGER(KIND=JPIM),INTENT(IN)    :: KST
INTEGER(KIND=JPIM),INTENT(IN)    :: KEND
INTEGER (KIND=JPIM), INTENT (IN) :: KFLEVG



  YDMF_PHYS_SURF%GSD_VH%PPSL(KST:KEND) = YDMF_PHYS%OUT%FPLSL(KST:KEND,KFLEVG)
  YDMF_PHYS_SURF%GSD_VH%PPCL(KST:KEND) = YDMF_PHYS%OUT%FPLCL(KST:KEND,KFLEVG)
  YDMF_PHYS_SURF%GSD_VH%PPSN(KST:KEND) = YDMF_PHYS%OUT%FPLSN(KST:KEND,KFLEVG)
  YDMF_PHYS_SURF%GSD_VH%PPCN(KST:KEND) = YDMF_PHYS%OUT%FPLCN(KST:KEND,KFLEVG)
  YDMF_PHYS_SURF%GSD_VH%PEVA(KST:KEND) = YDMF_PHYS%OUT%FEVN(KST:KEND,1)-YDMF_PHYS%OUT%FEVL(KST:KEND,1)
  

END SUBROUTINE
