SUBROUTINE MF_PHYS_CORWAT (YDCPG_DIM, POUT_FEVL, POUT_FEVN, POUT_FPLCL, POUT_FPLCN, POUT_FPLSL, POUT_FPLSN, YDMF_PHYS_SURF)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE
USE MF_PHYS_TYPE_MOD   , ONLY : MF_PHYS_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY : MF_PHYS_SURF_TYPE


IMPLICIT NONE

TYPE(CPG_DIM_TYPE),INTENT(IN)  :: YDCPG_DIM
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FEVL (YDCPG_DIM%KLON, 1:YDCPG_DIM%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FEVN (YDCPG_DIM%KLON, 1:YDCPG_DIM%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLCL (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLCN (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLSL (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLSN (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
TYPE(MF_PHYS_SURF_TYPE),INTENT(INOUT) :: YDMF_PHYS_SURF


YDMF_PHYS_SURF%GSD_VH%PPSL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLSL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
YDMF_PHYS_SURF%GSD_VH%PPCL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLCL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
YDMF_PHYS_SURF%GSD_VH%PPSN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLSN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
YDMF_PHYS_SURF%GSD_VH%PPCN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLCN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
YDMF_PHYS_SURF%GSD_VH%PEVA(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FEVN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1)-POUT_FEVL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1)

END SUBROUTINE
