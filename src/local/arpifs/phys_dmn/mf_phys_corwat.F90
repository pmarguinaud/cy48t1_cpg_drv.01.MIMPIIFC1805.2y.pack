SUBROUTINE MF_PHYS_CORWAT (YDCPG_BNDS, YDCPG_OPTS, POUT_FEVL, POUT_FEVN, POUT_FPLCL, POUT_FPLCN, &
& POUT_FPLSL, POUT_FPLSN, PGSD_VH_PEVA, PGSD_VH_PPCL, PGSD_VH_PPCN, PGSD_VH_PPSL, PGSD_VH_PPSN)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_OPTS_TYPE_MOD   , ONLY : CPG_BNDS_TYPE, CPG_OPTS_TYPE


IMPLICIT NONE

TYPE(CPG_BNDS_TYPE),INTENT(IN) :: YDCPG_BNDS
TYPE(CPG_OPTS_TYPE),INTENT(IN) :: YDCPG_OPTS
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FEVL (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FEVN (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLCL (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLCN (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLSL (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLSN (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PEVA (YDCPG_OPTS%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPCL (YDCPG_OPTS%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPCN (YDCPG_OPTS%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPSL (YDCPG_OPTS%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPSN (YDCPG_OPTS%KLON)


PGSD_VH_PPSL(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA) = POUT_FPLSL(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,YDCPG_OPTS%KFLEVG)
PGSD_VH_PPCL(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA) = POUT_FPLCL(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,YDCPG_OPTS%KFLEVG)
PGSD_VH_PPSN(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA) = POUT_FPLSN(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,YDCPG_OPTS%KFLEVG)
PGSD_VH_PPCN(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA) = POUT_FPLCN(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,YDCPG_OPTS%KFLEVG)
PGSD_VH_PEVA(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA) = POUT_FEVN(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1)-POUT_FEVL(YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA,1)

END SUBROUTINE
