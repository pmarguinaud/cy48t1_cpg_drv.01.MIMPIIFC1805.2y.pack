SUBROUTINE MF_PHYS_CORWAT (YDCPG_DIM, POUT_FEVL, POUT_FEVN, POUT_FPLCL, POUT_FPLCN, POUT_FPLSL, POUT_FPLSN, PGSD_VH_PEVA, PGSD_VH_PPCL, PGSD_VH_PPCN, PGSD_VH_PPSL, PGSD_VH_PPSN)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE


IMPLICIT NONE

TYPE(CPG_DIM_TYPE),INTENT(IN)  :: YDCPG_DIM
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FEVL (YDCPG_DIM%KLON, 1:YDCPG_DIM%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FEVN (YDCPG_DIM%KLON, 1:YDCPG_DIM%KTSSG+1)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLCL (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLCN (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLSL (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN)  :: POUT_FPLSN (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PEVA (YDCPG_DIM%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPCL (YDCPG_DIM%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPCN (YDCPG_DIM%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPSL (YDCPG_DIM%KLON)
REAL(KIND=JPRB),  INTENT (OUT) :: PGSD_VH_PPSN (YDCPG_DIM%KLON)


PGSD_VH_PPSL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLSL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
PGSD_VH_PPCL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLCL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
PGSD_VH_PPSN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLSN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
PGSD_VH_PPCN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FPLCN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDCPG_DIM%KFLEVG)
PGSD_VH_PEVA(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA) = POUT_FEVN(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1)-POUT_FEVL(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1)

END SUBROUTINE
