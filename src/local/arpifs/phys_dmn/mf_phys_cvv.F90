SUBROUTINE MF_PHYS_CVV (YDCPG_DIM, YDCPG_OPTS, PCVV_T0, PCVV_T1, YDMODEL)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE, CPG_OPTS_TYPE
USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(CPG_DIM_TYPE),INTENT(IN)    :: YDCPG_DIM
TYPE(CPG_OPTS_TYPE),INTENT(IN)   :: YDCPG_OPTS
REAL (KIND=JPRB),  INTENT(IN)    :: PCVV_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB),  INTENT(OUT)   :: PCVV_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
TYPE(MODEL),       INTENT(IN)    :: YDMODEL


INTEGER (KIND=JPIM) :: JLEV
INTEGER (KIND=JPIM) :: JROF


!        2.11 Evolution of CVV (GY)
!             ---------------------

IF(YDMODEL%YRML_PHY_MF%YRPHY%LCVPGY) THEN
  DO JLEV=1,YDCPG_DIM%KFLEVG
    DO JROF=YDCPG_DIM%KIDIA,YDCPG_DIM%KFDIA
      PCVV_T1(JROF,JLEV)=PCVV_T0(JROF,JLEV)
    ENDDO
  ENDDO
ENDIF



END SUBROUTINE
