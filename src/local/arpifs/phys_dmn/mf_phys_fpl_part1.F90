SUBROUTINE MF_PHYS_FPL_PART1 (YDCPG_DIM, YDMF_PHYS, YDVARS, YDMODEL)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE
USE MF_PHYS_TYPE_MOD   , ONLY : MF_PHYS_TYPE
USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(CPG_DIM_TYPE),INTENT(IN)    :: YDCPG_DIM
TYPE(MF_PHYS_TYPE),INTENT(INOUT) :: YDMF_PHYS
TYPE(FIELD_VARIABLES),INTENT(INOUT) :: YDVARS
TYPE(MODEL)       ,INTENT(INOUT) :: YDMODEL


INTEGER (KIND=JPIM) :: JLEV
INTEGER (KIND=JPIM) :: JROF


  DO JROF=YDCPG_DIM%KIDIA,YDCPG_DIM%KFDIA
    YDMF_PHYS%TMP%APLPAR%PFL%FPLCH(JROF,0)=0.0_JPRB
    YDMF_PHYS%TMP%APLPAR%PFL%FPLSH(JROF,0)=0.0_JPRB
  ENDDO
  
  IF(YDMODEL%YRML_PHY_MF%YRPHY%LNEBN.OR.YDMODEL%YRML_PHY_MF%YRPHY%LNEBR.OR.YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN
    DO JLEV=1,YDCPG_DIM%KFLEVG
      DO JROF=YDCPG_DIM%KIDIA,YDCPG_DIM%KFDIA
        YDMF_PHYS%TMP%APLPAR%PFL%FPLCH(JROF,JLEV)=YDVARS%CPF%T0(JROF,JLEV)
      ENDDO
    ENDDO
  ENDIF
  
  IF(YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN
    DO JLEV=1,YDCPG_DIM%KFLEVG
      DO JROF=YDCPG_DIM%KIDIA,YDCPG_DIM%KFDIA
        YDMF_PHYS%TMP%APLPAR%PFL%FPLSH(JROF,JLEV)=YDVARS%SPF%T0(JROF,JLEV)
      ENDDO
    ENDDO
  ENDIF



END SUBROUTINE
