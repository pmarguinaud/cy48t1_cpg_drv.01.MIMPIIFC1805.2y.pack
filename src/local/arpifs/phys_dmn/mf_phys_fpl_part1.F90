SUBROUTINE MF_PHYS_FPL_PART1 (YDCPG_BNDS, YDCPG_OPTS, PPFL_FPLCH, PPFL_FPLSH, PCPF_T0, PSPF_T0, &
& YDMODEL)

USE YOMHOOK, ONLY : LHOOK, DR_HOOK
USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_OPTS_TYPE_MOD   , ONLY : CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(CPG_BNDS_TYPE),INTENT(IN)   :: YDCPG_BNDS
TYPE(CPG_OPTS_TYPE),INTENT(IN)   :: YDCPG_OPTS

REAL (KIND=JPRB),  INTENT(OUT)   :: PPFL_FPLCH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB),  INTENT(OUT)   :: PPFL_FPLSH (YDCPG_OPTS%KLON, 0:YDCPG_OPTS%KFLEVG)

REAL (KIND=JPRB),  INTENT(IN)    :: PCPF_T0 (YDCPG_OPTS%KLON,YDCPG_OPTS%KFLEVG)
REAL (KIND=JPRB),  INTENT(IN)    :: PSPF_T0 (YDCPG_OPTS%KLON,YDCPG_OPTS%KFLEVG)
TYPE(MODEL)       ,INTENT(IN)    :: YDMODEL


INTEGER (KIND=JPIM) :: JLEV
INTEGER (KIND=JPIM) :: JROF

REAL (KIND=JPRB) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('MF_PHYS_FPL_PART1', 0, ZHOOK_HANDLE)

DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
  PPFL_FPLCH(JROF,0)=0.0_JPRB
  PPFL_FPLSH(JROF,0)=0.0_JPRB
ENDDO

IF(YDMODEL%YRML_PHY_MF%YRPHY%LNEBN.OR.YDMODEL%YRML_PHY_MF%YRPHY%LNEBR.OR.YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN
  DO JLEV=1,YDCPG_OPTS%KFLEVG
    DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
      PPFL_FPLCH(JROF,JLEV)=PCPF_T0(JROF,JLEV)
    ENDDO
  ENDDO
ENDIF

IF(YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN
  DO JLEV=1,YDCPG_OPTS%KFLEVG
    DO JROF=YDCPG_BNDS%KIDIA,YDCPG_BNDS%KFDIA
      PPFL_FPLSH(JROF,JLEV)=PSPF_T0(JROF,JLEV)
    ENDDO
  ENDDO
ENDIF

IF (LHOOK) CALL DR_HOOK ('MF_PHYS_FPL_PART1', 1, ZHOOK_HANDLE)

END SUBROUTINE MF_PHYS_FPL_PART1
