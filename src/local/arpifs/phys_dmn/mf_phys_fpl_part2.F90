SUBROUTINE MF_PHYS_FPL_PART2 (YDCPG_DIM, PPFL_FPLCH, PPFL_FPLSH, PCPF_T1, PSPF_T1, YDMODEL)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE
USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(CPG_DIM_TYPE),INTENT(IN)    :: YDCPG_DIM

REAL (KIND=JPRB),  INTENT(IN)    :: PPFL_FPLCH (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB),  INTENT(IN)    :: PPFL_FPLSH (YDCPG_DIM%KLON, 0:YDCPG_DIM%KFLEVG)

REAL (KIND=JPRB),  INTENT(OUT)   :: PCPF_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB),  INTENT(OUT)   :: PSPF_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
TYPE(MODEL)       ,INTENT(IN)    :: YDMODEL


INTEGER (KIND=JPIM) :: JLEV
INTEGER (KIND=JPIM) :: JROF


!        2.9a Evolution of precipitation fluxes
!             ------------------------------------------

IF(YDMODEL%YRML_PHY_MF%YRPHY%LNEBN.OR.YDMODEL%YRML_PHY_MF%YRPHY%LNEBR.OR.YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN
  DO JLEV=1,YDCPG_DIM%KFLEVG
    DO JROF=YDCPG_DIM%KIDIA,YDCPG_DIM%KFDIA
      PCPF_T1(JROF,JLEV)=PPFL_FPLCH(JROF,JLEV)
    ENDDO
  ENDDO
ENDIF

IF(YDMODEL%YRML_PHY_MF%YRPHY%LRRGUST) THEN
  DO JLEV=1,YDCPG_DIM%KFLEVG
    DO JROF=YDCPG_DIM%KIDIA,YDCPG_DIM%KFDIA
      PSPF_T1(JROF,JLEV)=PPFL_FPLSH(JROF,JLEV)
    ENDDO
  ENDDO
ENDIF

END SUBROUTINE
