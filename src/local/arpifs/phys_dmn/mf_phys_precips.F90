SUBROUTINE MF_PHYS_PRECIPS (YDCPG_DIM, YDMF_PHYS_TMP, YDMF_PHYS_SURF, YDMODEL)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE
USE MF_PHYS_TYPE_MOD   , ONLY : MF_PHYS_TMP_TYPE
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY : MF_PHYS_SURF_TYPE
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(CPG_DIM_TYPE),INTENT(IN)    :: YDCPG_DIM
TYPE(MF_PHYS_TMP_TYPE),INTENT(INOUT) :: YDMF_PHYS_TMP
TYPE(MF_PHYS_SURF_TYPE),INTENT(INOUT) :: YDMF_PHYS_SURF
TYPE(MODEL)       ,INTENT(INOUT) :: YDMODEL




IF (YDMODEL%YRML_PHY_MF%YRPHY%LDPRECIPS) THEN
  YDMF_PHYS_SURF%GSD_XP%PPRECIP(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDMODEL%YRML_PHY_MF% &
& YRPHY%YRDPRECIPS%           &
& NDTPRECCUR)=YDMF_PHYS_TMP%PRC%DPRECIPS(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDMODEL%YRML_PHY_MF% &
& YRPHY%YRDPRECIPS%           &
& NDTPRECCUR)
ENDIF

IF (YDMODEL%YRML_PHY_MF%YRPHY%LDPRECIPS2) THEN
  YDMF_PHYS_SURF%GSD_XP2%PPRECIP2(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDMODEL%YRML_PHY_MF% &
& YRPHY%YRDPRECIPS%NDTPRECCUR2)=YDMF_PHYS_TMP%PRC%DPRECIPS2(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,YDMODEL%YRML_PHY_MF% &
& YRPHY%YRDPRECIPS%NDTPRECCUR2)
ENDIF



END SUBROUTINE
