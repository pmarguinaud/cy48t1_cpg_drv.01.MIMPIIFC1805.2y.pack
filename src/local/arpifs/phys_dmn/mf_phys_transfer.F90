SUBROUTINE MF_PHYS_TRANSFER (YDCPG_DIM, PDAL_T0, PDAL_T1, PDOM_T0, PDOM_T1, PFQTUR_T0, PFQTUR_T1,   &
& PFSTUR_T0, PFSTUR_T1, PMXL_T0, PMXL_T1, PRKTH_T0, PRKTH_T1, PRKTQC_T0, PRKTQC_T1, PRKTQV_T0,      &
& PRKTQV_T1, PSHTUR_T0, PSHTUR_T1, PTTE_T0, PTTE_T1, PUAL_T0, PUAL_T1, PUEN_T0, PUEN_T1, PUNEBH_T0, &
& PUNEBH_T1, PUOM_T0, PUOM_T1, YDMODEL)

USE PARKIND1, ONLY : JPIM, JPRB

USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE
USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(CPG_DIM_TYPE),INTENT(IN)    :: YDCPG_DIM

REAL (KIND=JPRB), INTENT (IN   ) :: PDAL_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PDAL_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PDOM_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PDOM_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PFQTUR_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PFQTUR_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PFSTUR_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PFSTUR_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PMXL_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PMXL_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PRKTH_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PRKTH_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PRKTQC_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PRKTQC_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PRKTQV_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PRKTQV_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PSHTUR_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PSHTUR_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PTTE_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PTTE_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PUAL_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PUAL_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PUEN_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PUEN_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PUNEBH_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PUNEBH_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (IN   ) :: PUOM_T0 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
REAL (KIND=JPRB), INTENT (INOUT) :: PUOM_T1 (YDCPG_DIM%KLON,YDCPG_DIM%KFLEVG)
TYPE(MODEL)       ,INTENT(INOUT) :: YDMODEL





IF (YDMODEL%YRML_PHY_MF%YRPHY%LCVPRO.OR.YDMODEL%YRML_PHY_MF%YRPHY%LGPCMT) THEN
   IF (.NOT.YDMODEL%YRML_GCONF%YGFL%YUAL%LADV) THEN
      PUAL_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PUAL_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
      PUOM_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PUOM_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
   ENDIF
   IF (YDMODEL%YRML_PHY_MF%YRPHY%LCDDPRO) THEN
    IF (.NOT.YDMODEL%YRML_GCONF%YGFL%YDAL%LADV) THEN
      PDAL_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PDAL_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
      PDOM_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PDOM_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
    ENDIF
   ENDIF
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YUNEBH%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YUNEBH%LADV)THEN
   PUNEBH_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PUNEBH_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YUEN%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YUEN%LADV) THEN
   PUEN_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PUEN_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YTTE%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YTTE%LADV) THEN
   PTTE_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PTTE_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YMXL%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YMXL%LADV) THEN
   PMXL_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PMXL_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YSHTUR%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YSHTUR%LADV) THEN
   PSHTUR_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PSHTUR_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YFQTUR%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YFQTUR%LADV) THEN
   PFQTUR_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PFQTUR_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YFSTUR%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YFSTUR%LADV) THEN
   PFSTUR_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PFSTUR_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YRKTH%LACTIVE)THEN
   PRKTH_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PRKTH_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YRKTQV%LACTIVE)THEN
   PRKTQV_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PRKTQV_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YRKTQC%LACTIVE)THEN
   PRKTQC_T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)=PRKTQC_T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDCPG_DIM%KFLEVG)
ENDIF


END SUBROUTINE
