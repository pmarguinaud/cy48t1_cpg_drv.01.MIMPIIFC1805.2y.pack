SUBROUTINE MF_PHYS_TRANSFER (YDGEOMETRY, YDCPG_DIM, YDVARS, YDMODEL)

USE PARKIND1, ONLY : JPIM, JPRB

USE GEOMETRY_MOD       , ONLY : GEOMETRY
USE CPG_DIM_TYPE_MOD   , ONLY : CPG_DIM_TYPE
USE FIELD_VARIABLES_MOD, ONLY : FIELD_VARIABLES
USE TYPE_MODEL         , ONLY : MODEL


IMPLICIT NONE

TYPE(GEOMETRY)    ,INTENT(IN)    :: YDGEOMETRY
TYPE(CPG_DIM_TYPE),INTENT(IN)    :: YDCPG_DIM
TYPE(FIELD_VARIABLES),INTENT(INOUT) :: YDVARS
TYPE(MODEL)       ,INTENT(INOUT) :: YDMODEL





IF (YDMODEL%YRML_PHY_MF%YRPHY%LCVPRO.OR.YDMODEL%YRML_PHY_MF%YRPHY%LGPCMT) THEN
   IF (.NOT.YDMODEL%YRML_GCONF%YGFL%YUAL%LADV) THEN
      YDVARS%UAL%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%UAL%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
      YDVARS%UOM%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%UOM%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
   ENDIF
   IF (YDMODEL%YRML_PHY_MF%YRPHY%LCDDPRO) THEN
    IF (.NOT.YDMODEL%YRML_GCONF%YGFL%YDAL%LADV) THEN
      YDVARS%DAL%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%DAL%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
      YDVARS%DOM%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%DOM%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
    ENDIF
   ENDIF
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YUNEBH%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YUNEBH%LADV)THEN
   YDVARS%UNEBH%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%UNEBH%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YUEN%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YUEN%LADV) THEN
   YDVARS%UEN%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%UEN%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YTTE%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YTTE%LADV) THEN
   YDVARS%TTE%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%TTE%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YMXL%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YMXL%LADV) THEN
   YDVARS%MXL%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%MXL%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YSHTUR%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YSHTUR%LADV) THEN
   YDVARS%SHTUR%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%SHTUR%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YFQTUR%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YFQTUR%LADV) THEN
   YDVARS%FQTUR%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%FQTUR%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YFSTUR%LACTIVE.AND..NOT.YDMODEL%YRML_GCONF%YGFL%YFSTUR%LADV) THEN
   YDVARS%FSTUR%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%FSTUR%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YRKTH%LACTIVE)THEN
   YDVARS%RKTH%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%RKTH%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YRKTQV%LACTIVE)THEN
   YDVARS%RKTQV%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%RKTQV%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF
IF (YDMODEL%YRML_GCONF%YGFL%YRKTQC%LACTIVE)THEN
   YDVARS%RKTQC%T1(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)=YDVARS%RKTQC%T0(YDCPG_DIM%KIDIA:YDCPG_DIM%KFDIA,1:YDGEOMETRY%YRDIMV%NFLEVG)
ENDIF


END SUBROUTINE
